// ===== ЗАЧЕМ ЭТОТ ИНТЕРФЕЙС? =====
//
// Attackable («умеющий атаковать») — контракт для всех существ, способных атаковать.
// В нашей RPG атаковать могут: Warrior, Mage, Archer — и теоретически Enemy.
//
// Без интерфейса — нельзя написать универсальный код для атаки:
//   void processAttack(Warrior w) { ... }  // только для воина
//   void processAttack(Mage m) { ... }     // только для мага — дублирование!
//
// С интерфейсом — один метод работает для любого атакующего:
//   void processAttack(Attackable attacker) { ... }  // для всех сразу
//
// Это ключевой принцип объектно-ориентированного программирования:
// «Программируй на уровне интерфейса, а не реализации».
//
// Пакет rpg — все классы игры находятся в одном пакете (см. пояснение в DamageType.java).
package rpg;

// ===== ЧТО ТАКОЕ ИНТЕРФЕЙС В JAVA? =====
//
// interface (интерфейс) — контракт, определяющий набор способностей (методов).
//
// Чем интерфейс отличается от класса?
// - Класс (class) описывает КОНКРЕТНЫЙ объект: его данные (поля) и поведение (методы).
// - Интерфейс описывает СПОСОБНОСТЬ: что объект умеет делать, но не как именно.
//   Например, Attackable = «умеет атаковать». Кто угодно может атаковать: воин, маг, монстр.
//
// Класс РЕАЛИЗУЕТ интерфейс через ключевое слово implements:
//   public class Warrior implements Attackable { ... }
//
// Класс может реализовать НЕСКОЛЬКО интерфейсов (в отличие от наследования,
// где можно наследовать только один класс):
//   public class Paladin implements Attackable, Healable { ... }
//
// Зачем интерфейсы?
// 1. Полиморфизм — можно работать с любым Attackable, не зная конкретный класс:
//      Attackable attacker = new Warrior();  // или new Mage(), или new Dragon()
//      attacker.performAttack();             // вызовется метод конкретного класса
// 2. Разделение ответственности — интерфейс содержит только контракт, не реализацию.
// 3. Тестирование — легко подменить реализацию для тестов (mock-объекты).
//
// public — интерфейс доступен из любого пакета (см. модификаторы доступа в DamageType.java).
public interface Attackable {

    // Абстрактный метод — метод БЕЗ тела (без фигурных скобок { }).
    // В интерфейсе все методы без тела — неявно public и abstract.
    // Писать «public abstract» можно, но не принято — это и так подразумевается.
    //
    // Любой класс, реализующий Attackable, ОБЯЗАН написать тело этого метода.
    // Если не напишет — ошибка компиляции.
    //
    // DamageType — тип возвращаемого значения (sealed interface, см. DamageType.java).
    // Метод возвращает объект, описывающий тип и силу атаки персонажа.
    // Каждый персонаж реализует по-своему: воин вернёт Physical, маг — Magical.
    DamageType performAttack();

    // default-метод — метод С реализацией прямо в интерфейсе (Java 8+).
    //
    // Зачем он нужен?
    // 1. Предоставляет реализацию «по умолчанию» — классы могут НЕ переопределять этот метод.
    //    Если класс не переопределит getBattleCry(), он автоматически вернёт "В атаку!".
    // 2. Позволяет добавлять новые методы в интерфейс, не ломая существующие классы.
    //    Если бы все методы интерфейса были абстрактными, добавление нового метода
    //    заставило бы КАЖДЫЙ реализующий класс его написать — это сломало бы весь код.
    //
    // Класс МОЖЕТ переопределить default-метод, если нужна своя логика:
    //   @Override
    //   public String getBattleCry() { return "За Орду!"; }
    //
    // String — возвращает текстовую строку (боевой клич персонажа).
    default String getBattleCry() {
        return "В атаку!";
    }

    // static-метод в интерфейсе (Java 8+) — метод, принадлежащий ИНТЕРФЕЙСУ, а не объекту.
    //
    // Чем отличается от обычного метода?
    // - Обычный метод вызывается через объект: warrior.performAttack()
    // - static-метод вызывается через ИМЯ ИНТЕРФЕЙСА: Attackable.calculateCritDamage(50)
    //   Объект не нужен. Нельзя вызвать через экземпляр: warrior.calculateCritDamage(50) — ошибка!
    //
    // Зачем static-методы в интерфейсе?
    // Для утилитарных (вспомогательных) функций, логически связанных с интерфейсом.
    // Раньше такие методы помещали в отдельный класс-утилиту (например, AttackUtils).
    // Теперь можно хранить прямо в интерфейсе — удобнее искать и логичнее организовано.
    //
    // int baseDamage — параметр метода: базовый урон до критического удара.
    // return baseDamage * 2 — критический удар удваивает урон.
    static int calculateCritDamage(int baseDamage) {
        return baseDamage * 2;
    }
}
