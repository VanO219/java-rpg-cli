// ===== ЗАЧЕМ ЭТОТ ФАЙЛ? =====
//
// DamageType — тип урона в бою. Каждая атака наносит урон определённого вида:
// физический (Physical), магический (Magical) или смешанный (Mixed).
//
// Почему не простой int или String?
//   int damage = 25;       ← непонятно: физический? магический? смешанный?
//   String type = "fire";  ← нет гарантии корректности: "fyre", "Fire", "" — всё пройдёт.
//
// DamageType — типобезопасная иерархия: компилятор не позволит создать «неизвестный» тип урона.
// Кроме того, каждый вид хранит РАЗНЫЕ данные: Magical — ещё и стихию (element).
// Это невозможно выразить через enum (все константы одного типа), зато легко через sealed interface.
//
// Пакет (package) — группирует связанные классы в одно пространство имён.
// Имя пакета соответствует структуре папок: rpg → папка src/rpg/.
// Все классы в одном пакете могут обращаться друг к другу без импортов.
package rpg;

// sealed interface — запечатанный интерфейс (Java 17+).
//
// Что такое интерфейс (interface)?
// Это контракт — набор методов, которые обязан реализовать любой класс,
// решивший «подписаться» на этот интерфейс (через implements).
// Интерфейс описывает ЧТО нужно уметь делать, но не КАК.
//
// Что означает sealed (запечатанный)?
// Обычный интерфейс может реализовать кто угодно — любой класс в любом пакете.
// sealed ограничивает список наследников: ТОЛЬКО перечисленные классы могут его реализовать.
// Список наследников указывается после ключевого слова permits.
//
// Где здесь permits?
// В данном случае permits НЕ указан явно. Когда все реализации объявлены
// ВНУТРИ самого интерфейса (как вложенные типы), Java разрешает опустить permits.
// Компилятор сам понимает: наследники — это Physical, Magical и Mixed.
// Полная форма выглядела бы так:
//   public sealed interface DamageType permits Physical, Magical, Mixed { ... }
//
// Зачем sealed?
// 1. Компилятор ЗНАЕТ все возможные реализации → может проверить полноту switch.
//    Если добавить новый тип урона и забыть обработать его в switch — ошибка компиляции.
// 2. Паттерн «типобезопасная иерархия»: фиксированный набор вариантов,
//    как enum, но каждый вариант может хранить РАЗНЫЕ данные.
//    Enum не подходит, когда варианты отличаются набором полей.
//
// Раньше (до Java 17) для этого использовали абстрактный класс + наследники,
// но компилятор не мог проверить, что в switch обработаны все варианты.
public sealed interface DamageType {

    // Абстрактный метод интерфейса — метод БЕЗ тела (без фигурных скобок).
    // Каждый класс, реализующий DamageType, ОБЯЗАН предоставить реализацию этого метода.
    // int — примитивный тип, целое число (от -2^31 до 2^31-1).
    // Метод возвращает суммарный урон, независимо от типа (физический, магический, смешанный).
    int totalDamage();

    // record — неизменяемый класс данных (Java 16+).
    // Компилятор автоматически генерирует:
    //   - конструктор со всеми полями: new Physical(25)
    //   - геттер без префикса get: physical.amount() (а НЕ getAmount())
    //   - equals() и hashCode() по всем полям
    //   - toString() вида "Physical[amount=25]"
    // Все поля record — неявно private и final (нельзя изменить после создания).
    //
    // implements DamageType — этот record реализует запечатанный интерфейс DamageType.
    // Значит, Physical ОБЯЗАН реализовать метод totalDamage().
    // record неявно является final — его нельзя наследовать дальше.
    // Это одно из требований sealed: наследник должен быть final, sealed или non-sealed.
    //
    // Раньше (до Java 16) вместо record писали обычный class с полями,
    // конструктором, геттерами, equals(), hashCode() и toString() вручную — ~30 строк кода.
    // Record заменяет всё это одной строкой.
    //
    // Physical — чисто физический урон (удар мечом, стрела).
    // int amount — величина урона.
    record Physical(int amount) implements DamageType {

        // @Override — аннотация-маркер: сообщает компилятору, что этот метод
        // переопределяет (реализует) метод из интерфейса DamageType.
        // Если допустить опечатку в имени метода — компилятор выдаст ошибку.
        // Всегда ставь @Override при переопределении — это защита от ошибок.
        @Override
        public int totalDamage() { return amount; }
    }

    // record Magical (см. подробное объяснение record выше, в Physical).
    // Магический урон — имеет величину и стихию (огонь, лёд, молния и т.д.).
    //
    // String — класс для текстовых строк. В Java строки — это объекты, не примитивы.
    // String является неизменяемым (immutable) — после создания изменить нельзя.
    // int amount — величина магического урона.
    // String element — стихия магии (например, "огонь", "лёд", "молния").
    record Magical(int amount, String element) implements DamageType {

        // @Override — реализация метода totalDamage() из интерфейса (см. пояснение выше).
        @Override
        public int totalDamage() { return amount; }
    }

    // record Mixed (см. подробное объяснение record выше, в Physical).
    // Смешанный урон — комбинация физического и магического.
    //
    // int physical — физическая составляющая урона.
    // int magical — магическая составляющая урона.
    record Mixed(int physical, int magical) implements DamageType {

        // @Override — реализация метода totalDamage() из интерфейса (см. пояснение выше).
        // Здесь суммарный урон = физический + магический.
        @Override
        public int totalDamage() { return physical + magical; }
    }
}
