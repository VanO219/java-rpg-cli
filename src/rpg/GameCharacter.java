    // ===== toString() — ТЕКСТОВОЕ ПРЕДСТАВЛЕНИЕ ОБЪЕКТА =====
    //
    // toString() — метод класса Object (корневой класс ВСЕХ классов в Java).
    // Каждый класс в Java неявно наследует Object, а значит имеет toString(),
    // equals() и hashCode().
    //
    // По умолчанию toString() возвращает что-то вроде "rpg.GameCharacter@1a2b3c"
    // (имя класса + адрес в памяти) — бесполезно для отладки.
    //
    // Переопределяем, чтобы получить читаемый вывод:
    // "Воин Артур [HP: 120/120 ATK: 15 DEF: 10 LVL: 1]"
    //
    // System.out.println(hero) автоматически вызывает hero.toString().
    // Конкатенация строк "текст" + hero тоже вызывает hero.toString().

    /**
     * Текстовое представление персонажа для вывода в консоль.
     * Переопределяет метод Object.toString().
     *
     * @return строка вида "Класс Имя [HP: X/Y ATK: Z DEF: W LVL: N]"
     */
    @Override
    public String toString() {
        // getClassName() — вызов абстрактного метода.
        // В runtime будет вызвана реализация из конкретного наследника (полиморфизм).
        // + — оператор конкатенации строк: соединяет строки.
        // При + с числом Java автоматически преобразует число в строку.
        return getClassName() + " " + name +
                " [HP: " + health + "/" + maxHealth +
                " ATK: " + attack +
                " DEF: " + defense +
                " LVL: " + level + "]";
    }

    // ===== equals() — СРАВНЕНИЕ ОБЪЕКТОВ ПО СОДЕРЖИМОМУ =====
    //
    // equals() — метод из класса Object, определяет «логическое равенство» объектов.
    //
    // По умолчанию: equals() сравнивает ССЫЛКИ (как ==), т.е. два объекта равны
    // только если это ОДИН И ТОТ ЖЕ объект в памяти.
    //
    // Переопределяем, чтобы два персонажа считались равными, если у них
    // одинаковое имя и класс (два «Воин Артур» — это один и тот же персонаж).
    //
    // Контракт equals() (правила, которые ОБЯЗАТЕЛЬНО соблюдать):
    //   1. Рефлексивность: x.equals(x) == true
    //   2. Симметричность: x.equals(y) == y.equals(x)
    //   3. Транзитивность: если x.equals(y) и y.equals(z), то x.equals(z)
    //   4. x.equals(null) == false
    //   5. Если переопределяешь equals() — ОБЯЗАТЕЛЬНО переопредели hashCode()!

    // ===== ПРОЕКТНОЕ РЕШЕНИЕ: EQUALS ПО ИДЕНТИЧНОСТИ, А НЕ ПО СОСТОЯНИЮ =====
    //
    // Есть два подхода к реализации equals() для игровых персонажей:
    //
    // 1. Равенство по СОСТОЯНИЮ (state equality):
    //    Сравниваем ВСЕ поля: name, health, level, attack, defense, experience...
    //    Проблема: персонаж после боя (health=50) ≠ он же до боя (health=100).
    //    Получается, что один и тот же персонаж «не равен сам себе» после
    //    любого изменения. Это ломает работу коллекций (HashSet, HashMap),
    //    если объект меняется после добавления в коллекцию.
    //
    // 2. Равенство по ИДЕНТИЧНОСТИ (identity equality):
    //    Сравниваем только «кто это» — имя + класс персонажа.
    //    Воин «Артас» остаётся тем же персонажем независимо от текущего здоровья
    //    или уровня. Это соответствует реальной логике игры: персонаж —
    //    это сущность (entity), а не набор данных (value object).
    //
    // Мы выбрали подход 2 (identity equality), потому что GameCharacter —
    // это СУЩНОСТЬ с изменяемым состоянием. Для сущностей принято сравнивать
    // по идентификатору (в нашем случае — имя + класс), а не по всем полям.
    //
    // Подход 1 (state equality) подходит для ОБЪЕКТОВ-ЗНАЧЕНИЙ (value objects),
    // например record BattleStats — неизменяемые данные, где важно каждое поле.
    //
    // Правило: сущности (Entity) → equals по идентификатору.
    //          Значения (Value Object, record) → equals по всем полям.

    /**
     * Сравнивает персонажей по имени и классу.
     * Переопределяет Object.equals().
     *
     * @param obj объект для сравнения (может быть любого типа или null)
     * @return true, если имя и класс персонажей совпадают
     */
    @Override
    public boolean equals(Object obj) {
        // Быстрая проверка: если это тот же самый объект в памяти — точно равны.
        // == для объектов сравнивает ССЫЛКИ (адреса в памяти), а не содержимое.
        if (this == obj) return true;

        // ===== instanceof С PATTERN VARIABLE (Java 16+) =====
        //
        // obj instanceof GameCharacter other — ОДНОВРЕМЕННО:
        //   1. Проверяет: является ли obj экземпляром GameCharacter (или его наследника)?
        //   2. Если да — создаёт переменную other типа GameCharacter (pattern variable).
        //
        // !(obj instanceof GameCharacter other) — если obj == null ИЛИ не GameCharacter,
        // то возвращаем false. Проверка на null встроена — instanceof всегда false для null.
        //
        // Раньше (до Java 16) нужно было писать:
        //   if (!(obj instanceof GameCharacter)) return false;
        //   GameCharacter other = (GameCharacter) obj;
        // Теперь это одна строка — безопаснее и короче.
        if (!(obj instanceof GameCharacter other)) return false;

        // Objects.equals(a, b) — null-безопасное сравнение.
        // Если a == null и b == null → true. Если только один null → false.
        // Обычный a.equals(b) выбросил бы NullPointerException, если a == null.
        return Objects.equals(name, other.name) &&
                Objects.equals(getClassName(), other.getClassName());
    }

    // ===== hashCode() — ХЕШ-КОД ОБЪЕКТА =====
    //
    // hashCode() — метод из класса Object, возвращает числовой «отпечаток» объекта.
    //
    // Зачем нужен? Коллекции HashMap, HashSet используют hashCode() для быстрого поиска.
    // Алгоритм: сначала вычисляется hashCode(), потом в группе с таким хешем —
    // точное сравнение через equals().
    //
    // ГЛАВНОЕ ПРАВИЛО: если два объекта equals() == true,
    // их hashCode() ОБЯЗАН быть одинаковым.
    // Нарушение этого правила → HashMap/HashSet работают некорректно!
    //
    // Поэтому hashCode() использует ТЕ ЖЕ поля, что и equals() (name, getClassName).

    /**
     * Вычисляет хеш-код на основе имени и класса персонажа.
     * Переопределяет Object.hashCode().
     * Использует те же поля, что и equals() — это обязательное требование.
     *
     * @return числовой хеш-код
     */
    @Override
    public int hashCode() {
        // Objects.hash(значения...) — удобный метод для вычисления хеш-кода
        // из нескольких полей. Принимает любое количество аргументов (varargs).
        // Внутри вычисляет комбинированный хеш по формуле на основе Arrays.hashCode().
        return Objects.hash(name, getClassName());
    }

    // ===== getSpeed() — СКОРОСТЬ ПЕРСОНАЖА (для PriorityQueue, глава 5.7) =====
    //
    // Скорость определяет порядок хода в бою (инициативу).
    // Формула: базовая скорость (10) + бонус от уровня + бонус от ловкости (attack / 2).
    // У разных классов разная скорость: Лучник быстрее (высокая атака → высокий бонус),
    // Воин медленнее (но зато сильнее).
    //
    // Этот метод используется в PriorityQueue<GameCharacter> для определения
    // порядка ходов: кто быстрее — ходит первым.

    /**
     * Возвращает скорость персонажа (для определения порядка ходов в бою).
     * Формула: 10 + level + attack / 2.
     *
     * @return скорость персонажа (чем больше, тем быстрее)
     */
    public int getSpeed() {
        return 10 + level + attack / 2;
    }

    // ===== ПОДСТАНОВОЧНЫЕ ЗНАКИ (Wildcards) И ОГРАНИЧЕНИЯ ОБОБЩЕНИЙ =====
    //
    // ? (wildcard, подстановочный знак) — обозначает «неизвестный тип» в дженериках.
    // Используется, когда метод должен работать с ЛЮБЫМ параметром типа.
    //
    // Три вида wildcards:
    //
    // 1. ? extends T (верхняя граница, Upper Bounded Wildcard)
    //    Inventory<? extends GameCharacter> — инвентарь ЛЮБОГО типа, который ЯВЛЯЕТСЯ GameCharacter или его наследником.
    //    Можно ЧИТАТЬ элементы (как GameCharacter), но НЕЛЬЗЯ ДОБАВЛЯТЬ (компилятор не знает точный тип).
    //    Запоминай: extends = для ЧТЕНИЯ (producer).
    //
    // 2. ? super T (нижняя граница, Lower Bounded Wildcard)
    //    List<? super Warrior> — список ЛЮБОГО типа, который является Warrior ИЛИ его РОДИТЕЛЕМ.
    //    Можно ДОБАВЛЯТЬ Warrior и его наследников, но при чтении получаешь Object.
    //    Запоминай: super = для ЗАПИСИ (consumer).
    //
    // 3. ? (неограниченный wildcard)
    //    List<?> — список чего угодно. Нельзя ничего добавить (кроме null). Только чтение как Object.
    //
    // Мнемоника PECS: Producer Extends, Consumer Super.
    //
    // ===== ОГРАНИЧЕНИЯ ОБОБЩЕНИЙ (Bounded Type Parameters) =====
    //
    // <T extends Comparable<T>> — параметр типа T ОБЯЗАН реализовать интерфейс Comparable<T>.
    // Это ограничение (bound) гарантирует, что объекты типа T можно сравнивать друг с другом.
    // Без ограничения компилятор не позволит вызвать a.compareTo(b) — он не знает, что T это умеет.
    //
    // extends в дженериках означает И extends, И implements.
    // <T extends Comparable<T>> работает даже если Comparable — интерфейс (а не класс).

    /**
     * Находит персонажа с наибольшим здоровьем из массива.
     * Демонстрирует bounded type parameter: T extends GameCharacter.
     * T может быть Warrior, Mage, Archer или любым другим наследником GameCharacter.
     *
     * @param characters массив персонажей (не пустой)
     * @param <T> тип, ограниченный GameCharacter или его наследниками
     * @return персонаж с максимальным здоровьем
     */
    public static <T extends GameCharacter> T findStrongest(T[] characters) {
        // assert защищает от вызова с пустым или null массивом.
        // Без этой проверки characters[0] бросит ArrayIndexOutOfBoundsException или NPE.
        assert characters != null && characters.length > 0 : "Массив персонажей не должен быть пустым";

        // T extends GameCharacter гарантирует: у каждого элемента есть метод getHealth().
        // Без ограничения (просто <T>) вызов t.getHealth() вызвал бы ошибку компиляции —
        // компилятор не знал бы, что T имеет метод getHealth().
        T strongest = characters[0];

        // ===== for-ЦИКЛ СО СЧЁТЧИКОМ =====
        //
        // for (инициализация; условие; шаг) { тело }
        //   int i = 1     — начинаем с индекса 1 (индекс 0 уже взят как strongest)
        //   i < length     — повторяем, пока i меньше длины массива
        //   i++            — после каждой итерации увеличиваем i на 1
        //
        // Массивы в Java индексируются с 0: characters[0] — первый элемент.
        // characters.length — свойство массива (НЕ метод, без скобок!), возвращает размер.
        for (int i = 1; i < characters.length; i++) {
            if (characters[i].getHealth() > strongest.getHealth()) {
                strongest = characters[i];
            }
        }

        // Возвращаемый тип — T (не GameCharacter!).
        // Благодаря дженерику метод СОХРАНЯЕТ конкретный тип:
        //   Warrior[] warriors = { w1, w2, w3 };
        //   Warrior best = findStrongest(warriors);  // возвращает Warrior, не GameCharacter!
        // Без дженерика пришлось бы делать приведение типа: (Warrior) findStrongest(warriors).
        return strongest;
    }
