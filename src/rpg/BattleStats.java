// ===== ЗАЧЕМ ЭТОТ ФАЙЛ? =====
//
// BattleStats — статистика боя: что произошло за одно сражение.
// После победы над врагом игре нужно показать итоги: урон, потери, трофеи.
//
// Почему record, а не class?
// Статистика — данные только для чтения: после боя она не меняется.
// record автоматически делает все поля неизменяемыми (final), не нужно писать
// конструктор, геттеры, equals(), hashCode() вручную.
//
// Пакет rpg — все классы нашей RPG-игры (см. подробное объяснение в Enemy.java).
package rpg;

// record — неизменяемый класс данных (Java 16+).
//
// Раньше для хранения данных (DTO, value object) писали обычный class с:
//   - приватными полями
//   - конструктором
//   - геттерами (getName(), getHealth() и т.д.)
//   - toString(), equals(), hashCode()
// Это 50-100 строк шаблонного кода. record заменяет ВСЁ это одной строкой.
//
// Что компилятор генерирует АВТОМАТИЧЕСКИ для record:
//   1. Приватные final-поля для каждого компонента (characterName, totalDamageDealt и т.д.)
//   2. Канонический конструктор — принимает все компоненты как параметры.
//   3. Геттеры — БЕЗ префикса get: characterName(), а НЕ getCharacterName().
//   4. toString() — вида "BattleStats[characterName=Иван, totalDamageDealt=150, ...]"
//   5. equals() — сравнивает ВСЕ поля двух record-объектов.
//   6. hashCode() — вычисляется по ВСЕМ полям (для использования в HashMap/HashSet).
//
// Все поля record — неявно final: после создания объекта изменить их НЕЛЬЗЯ.
// Это делает record потокобезопасным (thread-safe) — можно безопасно передавать между потоками.
//
// Когда использовать record:
//   - Данные, которые не меняются после создания (статистика, результаты, DTO).
// Когда НЕ использовать:
//   - Объекты с изменяемым состоянием (игрок, враг — у них меняется здоровье).
//
// BattleStats — статистика боя: собирает итоги после завершения сражения.

// ===== record И final =====
//
// Record в Java НЕЯВНО является final классом.
// Это означает: public record BattleStats(...) эквивалентен public final record BattleStats(...)
// Писать final явно можно, но не нужно — компилятор и так это подразумевает.
//
// Почему record — final?
//   record гарантирует неизменяемость: все поля — final, нет сеттеров.
//   Если бы можно было наследовать record, наследник мог бы нарушить эту гарантию,
//   добавив изменяемые поля или переопределив методы. final предотвращает это.
//
// Другие неявные модификаторы record:
//   - Все поля (компоненты) — private final
//   - Класс — final (нельзя наследовать)
//   - Наследует java.lang.Record (а не Object напрямую)
//   - Нельзя объявлять дополнительные instance-поля (только static)
public record BattleStats(
        // Компоненты record (components) — перечисляются в скобках после имени.
        // Каждый компонент автоматически становится:
        //   - приватным final-полем
        //   - параметром конструктора
        //   - публичным геттером (метод с тем же именем)

        // String characterName — имя персонажа, чья статистика собрана.
        String characterName,

        // int totalDamageDealt — общий нанесённый урон за бой.
        int totalDamageDealt,

        // int totalDamageReceived — общий полученный урон за бой.
        int totalDamageReceived,

        // int enemiesDefeated — количество побеждённых врагов.
        int enemiesDefeated,

        // int healingDone — общее количество восстановленного здоровья.
        int healingDone
) {

    // === Компактный конструктор (compact constructor) ===

    // Компактный конструктор — особая форма конструктора, доступная ТОЛЬКО в record.
    // Отличие от обычного конструктора:
    //   - НЕТ скобок с параметрами: пишем public BattleStats { ... }, а НЕ public BattleStats(...) { ... }
    //   - Параметры уже доступны неявно (по именам компонентов record).
    //   - Присваивание this.поле = параметр происходит АВТОМАТИЧЕСКИ после выхода из блока { }.
    //     Писать this.characterName = characterName; НЕ НУЖНО (и нельзя).
    //
    // Зачем нужен компактный конструктор:
    //   - ВАЛИДАЦИЯ (проверка входных данных): убедиться, что данные корректны.
    //   - НОРМАЛИЗАЦИЯ (приведение к нужному виду): исправить данные перед сохранением.
    //
    // В этом конструкторе мы нормализуем: если урон отрицательный — заменяем на 0.
    // Это защита от ошибок: отрицательный урон не имеет смысла в игровой логике.
    public BattleStats {
        // Можно менять значения параметров (они ещё НЕ присвоены полям).
        // После выхода из блока компилятор выполнит: this.totalDamageDealt = totalDamageDealt;
        if (totalDamageDealt < 0) {
            totalDamageDealt = 0;
        }
        if (totalDamageReceived < 0) {
            totalDamageReceived = 0;
        }
        // Параметры characterName, enemiesDefeated, healingDone мы не проверяем —
        // они будут присвоены полям как есть (автоматически).
    }

    // === Кастомный метод в record ===

    // В record можно добавлять свои методы — это обычные методы, как в классе.
    // Нельзя только: добавлять новые поля (instance fields) или менять существующие
    // (они final). Можно: статические поля, статические методы и обычные методы.
    //
    // display — выводит статистику боя в читаемом формате.
    // void — метод ничего не возвращает, только выводит текст в консоль.
    //
    // Обратите внимание: внутри record мы обращаемся к полям НАПРЯМУЮ (characterName),
    // а не через геттеры (characterName()). Внутри класса оба варианта работают,
    // но прямой доступ к полям — проще и привычнее.
    public void display() {
        System.out.println("║  Герой: " + characterName);
        System.out.println("║  Нанесено урона: " + totalDamageDealt);
        System.out.println("║  Получено урона: " + totalDamageReceived);
        System.out.println("║  Врагов побеждено: " + enemiesDefeated);
        System.out.println("║  Исцеление: " + healingDone);
    }
}
