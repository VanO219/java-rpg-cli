// Пакет rpg (см. подробное объяснение package в GameCharacter.java).
package rpg;

// Наследование: Mage extends GameCharacter (см. подробное объяснение extends в Warrior.java).
// Маг — персонаж с низким здоровьем, но мощными магическими атаками.
//
// Mage — второй конкретный (не абстрактный) наследник GameCharacter.
// Как и Warrior, обязан реализовать абстрактные методы: getClassName() и specialAttack().
// Но реализация РАЗНАЯ — это и есть полиморфизм (см. объяснение в GameCharacter.java).
//
// Ресурс Мага — мана (магическая энергия). Сравни с яростью (rage) у Warrior.
// Каждый класс персонажа имеет свой уникальный ресурс — это пример ИНКАПСУЛЯЦИИ:
// mana и rage — private-поля, недоступные извне; логика их расхода — внутренняя деталь класса.
public class Mage extends GameCharacter {

    // ===== serialVersionUID — ВЕРСИЯ КЛАССА ДЛЯ СЕРИАЛИЗАЦИИ (глава 6.10) =====
    //
    // Mage наследует Serializable от GameCharacter — повторно писать implements не нужно.
    // Но serialVersionUID объявляется в КАЖДОМ классе иерархии отдельно, потому что
    // при десериализации JVM проверяет версию каждого класса в цепочке наследования:
    //   GameCharacter (serialVersionUID = 1L) → Mage (serialVersionUID = 1L)
    //
    // Если в будущем мы добавим Mage новое поле (например, spellBook), мы изменим
    // serialVersionUID только у Mage, не трогая GameCharacter.
    // Подробное объяснение serialVersionUID — см. GameCharacter.java.
    private static final long serialVersionUID = 1L;

    // Текущая мана (магическая энергия). Тратится при использовании заклинаний.
    // private — доступно только внутри Mage.
    private int mana;

    // ===== final НА ПОЛЕ — КОНСТАНТА ЭКЗЕМПЛЯРА =====
    //
    // final — значение поля нельзя изменить после инициализации.
    // После того как maxMana получит значение 100, переназначить его невозможно:
    //   this.maxMana = 200;  // Ошибка компиляции! final-поле нельзя изменить.
    //
    // ВАЖНО: final на ПОЛЕ экземпляра — это константа ОДНОГО объекта,
    // а не всех объектов класса. Каждый Mage имеет свой maxMana = 100.
    // Если бы нужна была ОБЩАЯ константа для всех — использовали бы static final:
    //   private static final int MAX_MANA = 100;
    //   (static — одна на всех, final — неизменяемая, имя КАПСОМ — соглашение Java для констант)
    //
    // final на ПЕРЕМЕННОЙ — значение нельзя переназначить.
    // final на КЛАССЕ    — класс нельзя наследовать (например, String — final).
    // final на МЕТОДЕ    — метод нельзя переопределить в наследнике.
    private final int maxMana = 100;

    /**
     * Создаёт мага с заданным именем.
     * Характеристики мага: HP=80, ATK=8, DEF=5 — низкая выживаемость,
     * но компенсируется мощной магической атакой (specialAttack).
     *
     * @param name имя мага
     */
    public Mage(String name) {
        // super() — вызов конструктора родителя GameCharacter
        // (см. подробное объяснение super в Warrior.java).
        super(name, 80, 8, 5);

        // this.mana — поле ЭТОГО объекта. maxMana — final-поле, уже равно 100.
        // При создании мага мана полная.
        this.mana = maxMana;
    }

    /**
     * Возвращает название класса — "Маг".
     * Реализация абстрактного метода GameCharacter.getClassName().
     *
     * @return строка "Маг"
     */
    @Override
    public String getClassName() {
        return "Маг";
    }

    /**
     * Специальная атака мага: заклинание, тратящее ману.
     * Реализация абстрактного метода GameCharacter.specialAttack().
     *
     * Механика:
     *   - Если маны >= 20: тратит 20 маны, наносит тройной магический урон ("Огонь").
     *   - Если маны < 20: слабая магическая атака без затрат ("Искра").
     *
     * @return Magical-урон (мощный или слабый, в зависимости от маны)
     */
    @Override
    public DamageType specialAttack() {
        // if-else — условная конструкция.
        // Условие в скобках вычисляется как boolean (true/false).
        // Если true — выполняется блок после if.
        // Если false — выполняется блок после else (если он есть).
        if (mana >= 20) {
            // -= — составной оператор: mana = mana - 20.
            mana -= 20;

            // getAttack() * 3 — тройной урон: базовая атака 8 * 3 = 24.
            // new DamageType.Magical(24, "Огонь") — создаём record Magical (см. DamageType.java).
            // Маг наносит МАГИЧЕСКИЙ урон (заклинанием), поэтому Magical, а не Physical.
            // Второй параметр (String element) — название стихии: у record Magical два поля
            // (amount, element), в отличие от Physical, где только одно (amount).
            // Это преимущество sealed interface + record перед enum:
            // каждый вариант может хранить РАЗНЫЙ набор данных.
            return new DamageType.Magical(getAttack() * 3, "Огонь");
        }

        // Маны недостаточно — слабая атака без затрат маны.
        // getAttack() = 8 (базовый урон мага, без множителя).
        // "Искра" — слабая стихия, показывает что маг ослаб.
        return new DamageType.Magical(getAttack(), "Искра");
    }

    /**
     * Боевой клич мага.
     * Переопределяет default-метод Attackable.getBattleCry()
     * (см. подробное объяснение переопределения default-метода в Warrior.java).
     *
     * @return "Сила стихий, повинуйтесь!"
     */
    @Override
    public String getBattleCry() {
        return "Сила стихий, повинуйтесь!";
    }

    // ===== super.toString() — ВЫЗОВ МЕТОДА РОДИТЕЛЯ =====
    //
    // Ключевое слово super позволяет обратиться к РОДИТЕЛЬСКОЙ версии метода.
    //
    // super в конструкторе: super(...) — вызов конструктора родителя (только первой строкой).
    // super в методе: super.метод() — вызов метода родителя (в любом месте метода).
    //
    // Здесь super.toString() вызывает GameCharacter.toString(),
    // который возвращает "Маг Мерлин [HP: 80/80 ATK: 8 DEF: 5 LVL: 1]".
    // К этой строке мы добавляем информацию о мане — уникальную для Mage.
    //
    // Зачем? Чтобы не дублировать код из GameCharacter.toString().
    // Принцип DRY (Don't Repeat Yourself) — не повторяй код, переиспользуй.

    /**
     * Текстовое представление мага: базовая информация + мана.
     * Переопределяет GameCharacter.toString(), дополняя его информацией о мане.
     *
     * @return строка вида "Маг Мерлин [HP: 80/80 ATK: 8 DEF: 5 LVL: 1] [Мана: 100/100]"
     */
    @Override
    public String toString() {
        // super.toString() — результат из GameCharacter: "Маг Мерлин [HP: 80/80 ...]"
        // + — конкатенация: добавляем блок с маной.
        return super.toString() + " [Мана: " + mana + "/" + maxMana + "]";
    }

    /**
     * Возвращает текущее количество маны.
     * Геттер для private-поля mana.
     *
     * @return текущая мана
     */
    public int getMana() {
        return mana;
    }

    // ===== СЕТТЕР ДЛЯ БИНАРНОЙ ДЕСЕРИАЛИЗАЦИИ (глава 6.7) =====
    //
    // При бинарной загрузке (DataInputStream) мы создаём нового Mage через конструктор,
    // а затем восстанавливаем каждое поле отдельно.
    // Конструктор Mage(name) устанавливает mana = maxMana (полная мана),
    // но сохранённое значение может быть другим (маг потратил ману в бою).
    // Без этого сеттера загруженная мана всегда была бы 100 — баг потери данных.
    //
    // Package-private видимость: доступен только из пакета rpg.
    void setMana(int mana) {
        this.mana = mana;
    }
}
