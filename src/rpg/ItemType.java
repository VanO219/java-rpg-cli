// Пакет rpg — все классы нашей RPG-игры (см. подробное объяснение в DamageType.java).
package rpg;

// ===== enum С АБСТРАКТНЫМ МЕТОДОМ =====
//
// Этот enum демонстрирует продвинутую возможность: абстрактные методы в enum.
//
// Обычный enum — все константы ведут себя одинаково (как в GameState).
// Но иногда каждая константа должна иметь СВОЁ уникальное поведение.
// Для этого используют абстрактные методы:
//   1. Объявляем абстрактный метод в enum: public abstract String describe();
//   2. КАЖДАЯ константа ОБЯЗАНА предоставить реализацию — иначе ошибка компиляции.
//   3. Реализация пишется в теле константы (в фигурных скобках { }).
//
// ===== КАК ЭТО РАБОТАЕТ ВНУТРИ? =====
//
// Когда константа имеет тело { }, Java создаёт АНОНИМНЫЙ ПОДКЛАСС этого enum.
// То есть POTION — это не просто экземпляр ItemType, а экземпляр анонимного класса,
// который наследует ItemType и переопределяет метод describe().
//
// Это похоже на анонимные классы (anonymous classes), которые используются
// для однократных реализаций интерфейсов (см. BattleEventListener.java).
//
// Это эквивалентно такому псевдокоду:
//   class ItemType$POTION extends ItemType {
//       @Override public String describe() { return "Восстанавливает здоровье или ману"; }
//   }
//   public static final ItemType POTION = new ItemType$POTION("Зелье");
//
// Разница между обычным методом и абстрактным в enum:
//   - Обычный метод: ОДНА реализация для всех констант (canTransitionTo в GameState).
//   - Абстрактный метод: СВОЯ реализация для каждой константы (describe здесь).
//
// ItemType описывает типы предметов в RPG-инвентаре.
public enum ItemType {

    // ===== КОНСТАНТЫ С ТЕЛОМ { } =====
    //
    // Каждая константа имеет блок { } с реализацией абстрактного метода describe().
    // Фигурные скобки после аргументов конструктора — тело анонимного подкласса.
    //
    // @Override — аннотация, подтверждающая переопределение метода.
    // Компилятор проверит, что describe() действительно объявлен как абстрактный в enum.

    // Зелье — расходуемый предмет для восстановления здоровья или маны.
    POTION("Зелье") {
        // Реализация describe() для зелий.
        // Каждая константа описывает себя по-своему.
        @Override
        public String describe() {
            return "Восстанавливает здоровье или ману";
        }
    },

    // Оружие — экипируемый предмет, увеличивающий урон.
    WEAPON("Оружие") {
        @Override
        public String describe() {
            return "Увеличивает силу атаки";
        }
    },

    // Броня — экипируемый предмет, увеличивающий защиту.
    ARMOR("Броня") {
        @Override
        public String describe() {
            return "Увеличивает защиту";
        }
    },

    // Свиток — одноразовый магический предмет.
    SCROLL("Свиток") {
        @Override
        public String describe() {
            return "Одноразовое магическое заклинание";
        }
    };

    // ===== ПОЛЯ enum =====

    // Отображаемое название типа на русском языке.
    // private final — доступно только внутри класса и неизменяемо (см. пояснение в GameState.java).
    private final String displayName;

    // ===== КОНСТРУКТОР enum =====
    //
    // Конструктор enum всегда приватный (см. подробное объяснение в GameState.java).
    // Здесь один параметр — displayName.
    ItemType(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Возвращает отображаемое название типа предмета.
     *
     * @return строка с названием типа на русском языке
     */
    public String getDisplayName() {
        return displayName;
    }

    // ===== АБСТРАКТНЫЙ МЕТОД В enum =====
    //
    // abstract — метод без тела. Каждая константа ОБЯЗАНА предоставить реализацию.
    // Если хотя бы одна константа не реализует describe() — ошибка компиляции.
    //
    // Это отличается от абстрактного метода в классе:
    //   - В классе: абстрактный метод реализуют НАСЛЕДНИКИ (подклассы).
    //   - В enum: абстрактный метод реализуют КОНСТАНТЫ (в своих телах { }).
    //
    // Зачем абстрактный метод вместо switch?
    // Можно было бы написать обычный метод:
    //   public String describe() {
    //       return switch (this) {
    //           case POTION -> "Восстанавливает здоровье или ману";
    //           case WEAPON -> "Увеличивает силу атаки";
    //           ...
    //       };
    //   }
    // Но при абстрактном методе: если добавить новую константу и забыть describe() —
    // компилятор сразу покажет ошибку. При switch нужно помнить добавить case.

    /**
     * Возвращает описание типа предмета.
     * Абстрактный метод — каждая константа предоставляет своё описание.
     *
     * @return строка с описанием типа предмета
     */
    public abstract String describe();

    // ===== СТАТИЧЕСКИЙ ФАБРИЧНЫЙ МЕТОД =====
    //
    // Фабричный метод (factory method) — статический метод, который создаёт
    // (или в данном случае находит) объект по заданным параметрам.
    //
    // Зачем?
    // У enum есть встроенный valueOf("POTION"), но он ищет по ИМЕНИ КОНСТАНТЫ.
    // Наш fromDisplayName() ищет по ОТОБРАЖАЕМОМУ ИМЕНИ ("Зелье").
    // Это удобно для поиска по пользовательскому вводу или локализованному имени.
    //
    // ===== values() — ВСТРОЕННЫЙ МЕТОД enum =====
    //
    // values() — статический метод, автоматически генерируемый компилятором для каждого enum.
    // Возвращает массив ВСЕХ констант enum в порядке объявления:
    //   ItemType[] all = ItemType.values();  // [POTION, WEAPON, ARMOR, SCROLL]
    //
    // Массив (array) — контейнер фиксированного размера для хранения элементов одного типа.
    // ItemType[] — массив элементов типа ItemType.
    //
    // ===== for-each (улучшенный цикл for) =====
    //
    // for (ItemType type : values()) — перебирает все элементы массива по очереди.
    // Читается: «для каждого type из values()».
    // На каждой итерации переменная type получает следующий элемент массива.
    // Это безопаснее обычного for (int i = 0; ...): нет риска выйти за границы массива.

    /**
     * Находит тип предмета по отображаемому имени.
     * Статический фабричный метод — ищет среди всех констант совпадение по displayName.
     *
     * @param name отображаемое имя для поиска (например, "Зелье")
     * @return найденная константа ItemType
     * @throws IllegalArgumentException если тип с таким именем не найден
     */
    public static ItemType fromDisplayName(String name) {
        // Перебираем все константы enum через values().
        for (ItemType type : values()) {
            // String.equals() — сравнение строк ПО СОДЕРЖИМОМУ.
            // НЕЛЬЗЯ сравнивать строки через == !
            // == сравнивает ССЫЛКИ (адреса в памяти), а не содержимое.
            // "Зелье" == "Зелье" может быть false, если строки в разных местах памяти.
            // "Зелье".equals("Зелье") всегда true.
            //
            // type.displayName — прямой доступ к полю (внутри класса можно без геттера).
            if (type.displayName.equals(name)) {
                return type;
            }
        }
        // throw — оператор, который ВЫБРАСЫВАЕТ исключение (exception).
        // Исключение — объект, описывающий ошибочную ситуацию.
        //
        // IllegalArgumentException — стандартное исключение Java, означающее:
        // «метод получил недопустимый аргумент».
        // Это unchecked-исключение (наследник RuntimeException) —
        // вызывающий код НЕ обязан его ловить (но может).
        //
        // new IllegalArgumentException("...") — создаём объект исключения с сообщением.
        // После throw выполнение метода прекращается, управление передаётся обработчику
        // (блоку try-catch) или, если его нет, программа завершается с ошибкой.
        throw new IllegalArgumentException("Неизвестный тип предмета: " + name);
    }
}
