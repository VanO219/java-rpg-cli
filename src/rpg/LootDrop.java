// Пакет rpg — все классы нашей RPG-игры (см. подробное объяснение в DamageType.java).
package rpg;

// ===== record ДЛЯ ДАННЫХ ЛУТА (глава 5.2, 5.8) =====
//
// LootDrop — описание одного возможного «дропа» (награды) с врага.
//
// record — неизменяемый класс данных (Java 16+).
// (Подробное объяснение record — см. BattleStats.java)
// Компилятор автоматически создаёт:
//   - конструктор: new LootDrop("Зелье здоровья", 10, 0, 0.5)
//   - геттеры без префикса get: itemName(), gold(), gems(), chance()
//   - equals() по ВСЕМ полям: два LootDrop равны, если все 4 поля совпадают.
//   - hashCode() по ВСЕМ полям: для корректной работы в HashMap, HashSet.
//   - toString(): "LootDrop[itemName=Зелье здоровья, gold=10, gems=0, chance=0.5]"
//   - все поля — private final (неизменяемые после создания).
//
// ===== КАК LootDrop ИСПОЛЬЗУЕТСЯ В КОЛЛЕКЦИЯХ (глава 5.8) =====
//
// LootDrop используется как ЗНАЧЕНИЕ (value) в Map:
//   Map<EnemyRank, List<LootDrop>> lootTable
//
// Это структура «таблица лута» (loot table):
//   Ключ (Key):    EnemyRank (COMMON, ELITE, BOSS) — ранг врага.
//   Значение (Value): List<LootDrop> — список возможных наград для этого ранга.
//
// Пример содержимого:
//   COMMON → [LootDrop("Зелье", 5, 0, 0.7), LootDrop("Монеты", 10, 0, 0.5)]
//   ELITE  → [LootDrop("Эликсир", 20, 1, 0.6), LootDrop("Самоцвет", 0, 3, 0.3)]
//   BOSS   → [LootDrop("Легендарный меч", 50, 5, 0.8)]
//
// HashMap использует equals() и hashCode() для работы с ключами.
// Для ЗНАЧЕНИЙ (List<LootDrop>) equals/hashCode не критичны при доступе,
// но важны при сравнении списков между собой (например, в тестах).
public record LootDrop(
        // Название предмета-награды (например, "Зелье здоровья", "Эликсир маны").
        // String — ссылочный тип, хранит неизменяемую строку (immutable).
        String itemName,

        // Количество золота в награде.
        // int — примитивный 32-битный целочисленный тип.
        // Примитивы (int, long, double, boolean) НЕ могут быть null.
        // Если нужна возможность null — используют обёртку Integer (но здесь это не нужно).
        int gold,

        // Количество самоцветов (gems) в награде.
        // Самоцветы — редкая валюта, обычно 0 у обычных врагов.
        int gems,

        // Шанс выпадения этого лута: 0.0 (0%) .. 1.0 (100%).
        // double — 64-битное число с плавающей точкой (тип для дробных чисел).
        //
        // Почему диапазон 0.0–1.0, а не 0–100?
        //   Вероятность в математике выражается числом от 0 до 1.
        //   0.0 = невозможно (0%), 0.5 = 50/50, 1.0 = гарантировано (100%).
        //   Удобно для умножения: baseDamage * chance — сразу получаем ожидаемый урон.
        //   Перевод в проценты: chance * 100 (0.75 → 75%).
        //
        // ВНИМАНИЕ: double имеет ограниченную точность!
        //   0.1 + 0.2 == 0.30000000000000004 (не 0.3!)
        //   Для финансовых расчётов используют BigDecimal, но для шанса лута double достаточен.
        double chance
) {
    // ===== КОМПАКТНЫЙ КОНСТРУКТОР RECORD (Compact Constructor) =====
    //
    // Обычный конструктор record (канонический) выглядит так:
    //   public LootDrop(String itemName, int gold, int gems, double chance) {
    //       this.itemName = itemName;
    //       this.gold = gold;
    //       this.gems = gems;
    //       this.chance = chance;
    //   }
    //
    // Компактный конструктор — ОСОБЕННОСТЬ record (в обычных классах такого нет).
    // Записывается БЕЗ списка параметров в скобках: public LootDrop { ... }
    //
    // КАК ОН РАБОТАЕТ:
    //   1. Параметры доступны по именам полей: chance, gold, gems, itemName.
    //   2. Код компактного конструктора выполняется ДО присвоения значений полям.
    //   3. После выполнения нашего кода компилятор АВТОМАТИЧЕСКИ добавляет:
    //      this.itemName = itemName;
    //      this.gold = gold;     (возможно изменённое значение!)
    //      this.gems = gems;     (возможно изменённое значение!)
    //      this.chance = chance;
    //
    // ЗАЧЕМ КОМПАКТНЫЙ КОНСТРУКТОР?
    //   - Валидация: проверяем данные и бросаем исключение, если они некорректны.
    //   - Нормализация: корректируем данные перед сохранением в поля.
    //   - Минимум кода: не нужно писать this.field = field для каждого поля.
    //
    // ВАЖНО: в компактном конструкторе НЕЛЬЗЯ писать this.field = value напрямую!
    // Вместо этого переприсваиваем ПАРАМЕТР: gold = 0 (а не this.gold = 0).
    // Компилятор сам запишет изменённый параметр в поле после конструктора.
    public LootDrop {
        // ===== ВАЛИДАЦИЯ ДАННЫХ =====
        //
        // Проверяем, что шанс в допустимом диапазоне.
        // Если нет — бросаем IllegalArgumentException.
        //
        // IllegalArgumentException — стандартное непроверяемое (unchecked) исключение.
        // Используется когда аргумент метода/конструктора имеет недопустимое значение.
        // Unchecked = компилятор НЕ заставляет ловить через try-catch.
        //
        // throw new — создаёт и выбрасывает объект исключения.
        // Конструктор исключения принимает сообщение об ошибке (String).
        // Конкатенация строк (+): "текст" + chance → "текст" + "0.5" → "текст0.5".
        if (chance < 0.0 || chance > 1.0) {
            throw new IllegalArgumentException(
                    "Шанс должен быть от 0.0 до 1.0, получено: " + chance);
        }

        // ===== НОРМАЛИЗАЦИЯ ДАННЫХ =====
        //
        // Вместо исключения для отрицательного золота — нормализуем значение.
        // Это «мягкая» стратегия: исправляем ошибку, а не крашим программу.
        //
        // Переприсваиваем ПАРАМЕТР (gold = 0), а не поле (this.gold = 0).
        // Компилятор record запрещает this.field = в компактном конструкторе.
        // После завершения конструктора компилятор сам выполнит: this.gold = gold;
        if (gold < 0) {
            gold = 0; // Нормализация: отрицательное золото → 0
        }
        if (gems < 0) {
            gems = 0; // Нормализация: отрицательные самоцветы → 0
        }
    }
}
