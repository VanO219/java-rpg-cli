// ===== ЗАЧЕМ ЭТОТ ФАЙЛ? =====
//
// BattleRecord — одна строка в таблице рекордов игры.
// После прохождения игры результат сохраняется: кто, сколько очков, сколько врагов.
//
// Почему record + Comparable?
//   - record: результат неизменяем после записи — использование record логично.
//   - Comparable: таблица рекордов должна быть отсортирована — нужно уметь сравнивать записи.
//   - TreeSet<BattleRecord>: автоматически хранит в порядке compareTo() без ручной сортировки.
//
// Пакет rpg — все классы нашей RPG-игры (см. подробное объяснение в DamageType.java).
package rpg;

// ===== record + Comparable — СОРТИРУЕМАЯ ЗАПИСЬ (глава 5.5, 5.6) =====
//
// BattleRecord — результат одного прохождения игры (запись для таблицы рекордов).
// Хранит имя героя, счёт, количество побеждённых врагов и время прохождения.
//
// record — неизменяемый класс данных (Java 16+).
// Компилятор автоматически генерирует:
//   - конструктор со всеми полями (канонический конструктор)
//   - геттеры: heroName(), score(), enemiesDefeated(), timestamp() (без префикса get)
//   - equals() и hashCode() по ВСЕМ полям
//   - toString() вида "BattleRecord[heroName=Воин, score=1500, ...]"
// Все поля record — неявно private final (неизменяемые после создания).
// (Подробное объяснение record — см. BattleStats.java)
//
// ===== Comparable<T> — ИНТЕРФЕЙС ЕСТЕСТВЕННОГО ПОРЯДКА (Natural Ordering) =====
//
// implements Comparable<BattleRecord> — этот record «умеет» сравнивать себя с другим BattleRecord.
//
// Comparable<T> — интерфейс из пакета java.lang (не нужно импортировать).
// Содержит ОДИН абстрактный метод:
//   int compareTo(T other)
//
// Возвращаемое значение compareTo() определяет ПОРЯДОК двух объектов:
//   < 0  — этот объект МЕНЬШЕ other (идёт РАНЬШЕ при сортировке по возрастанию)
//   = 0  — объекты РАВНЫ (одинаковый порядок)
//   > 0  — этот объект БОЛЬШЕ other (идёт ПОЗЖЕ при сортировке по возрастанию)
//
// Пример: "Альфа".compareTo("Бета") → отрицательное число (А < Б в алфавите).
//
// ЗАЧЕМ НУЖЕН Comparable?
//   - Collections.sort(list) — отсортирует список объектов, реализующих Comparable.
//   - TreeSet<T> и TreeMap<K,V> — хранят элементы в порядке, определённом compareTo().
//   - Без Comparable или Comparator TreeSet бросит ClassCastException при добавлении!
//
// «Естественный порядок» (natural ordering) — это порядок, заданный методом compareTo().
// Для String — алфавитный, для Integer — числовой, для BattleRecord — по убыванию счёта.
//
// ===== КОНТРАКТ Comparable (ОБЯЗАТЕЛЬНЫЕ ПРАВИЛА) =====
//
// Контракт — это набор правил, которые ОБЯЗАТЕЛЬНО нужно соблюдать при реализации.
// Нарушение контракта → непредсказуемое поведение коллекций (TreeSet, sort и т.д.).
//
//   1. АНТИСИММЕТРИЯ: если a.compareTo(b) < 0, то b.compareTo(a) > 0 (и наоборот).
//   2. ТРАНЗИТИВНОСТЬ: если a < b и b < c, то a < c.
//   3. СОГЛАСОВАННОСТЬ: если a.compareTo(b) == 0, то a.compareTo(c) и b.compareTo(c) имеют одинаковый знак.
//   4. РЕКОМЕНДАЦИЯ (не строгое): если a.compareTo(b) == 0, то a.equals(b) == true.
//      Нарушение п.4 не ломает sort(), но ЛОМАЕТ TreeSet (см. ниже)!
//
// ===== КРИТИЧЕСКИ ВАЖНО: compareTo() и TreeSet =====
//
// TreeSet использует compareTo() (а НЕ equals()) для определения дубликатов!
//
// Если a.compareTo(b) == 0, TreeSet считает a и b ОДИНАКОВЫМИ элементами.
// Второй элемент НЕ будет добавлен, даже если a.equals(b) == false!
//
// Пример проблемы:
//   Два игрока: "Воин" с 1000 очков и "Маг" с 1000 очков.
//   Если compareTo() сравнивает ТОЛЬКО score → оба имеют score 1000 → compareTo() == 0.
//   TreeSet решит: это дубликат! "Маг" не будет добавлен в таблицу рекордов.
//
// РЕШЕНИЕ: многоуровневая сортировка (см. compareTo() ниже).
//   Сначала по score, при равенстве — по имени, при равенстве — по timestamp.
//   Так compareTo() == 0 только для полностью одинаковых записей.
//
// ВАЖНО: record автоматически генерирует equals() по ВСЕМ полям,
// а наш compareTo() сравнивает поля в определённом порядке с приоритетами.
// Это допустимая несогласованность, если мы понимаем последствия для TreeSet.
public record BattleRecord(
        // Имя героя, прошедшего игру. String — ссылочный тип (класс java.lang.String).
        String heroName,

        // Общий счёт (score) за прохождение.
        // long — 64-битное целое число (примитивный тип).
        // Диапазон: от -9_223_372_036_854_775_808 до 9_223_372_036_854_775_807.
        // Используем long вместо int (32 бита), чтобы исключить переполнение
        // при большом количестве прохождений с высоким счётом.
        long score,

        // Количество побеждённых врагов (0..5 в нашей игре).
        // int — 32-битное целое, достаточно для малого диапазона.
        int enemiesDefeated,

        // Временная метка завершения игры.
        // System.currentTimeMillis() — количество миллисекунд с 1 января 1970 года (Unix epoch).
        // long — необходим, т.к. значение не помещается в int (int переполнится в 2038 году).
        long timestamp
) implements Comparable<BattleRecord> {

    // ===== compareTo() — ОПРЕДЕЛЕНИЕ ЕСТЕСТВЕННОГО ПОРЯДКА =====
    //
    // Реализуем единственный метод интерфейса Comparable<BattleRecord>.
    // Этот метод определяет, как BattleRecord-ы сравниваются между собой.
    //
    // @Override — проверяет, что мы действительно переопределяем метод интерфейса.
    // Если написать compareTO (с заглавной O) — компилятор выдаст ошибку благодаря @Override.
    //
    // Наша сортировка — МНОГОУРОВНЕВАЯ (multi-level sorting):
    //   1. Первичный критерий: score (по УБЫВАНИЮ — лучший результат первым)
    //   2. Вторичный критерий: heroName (по ВОЗРАСТАНИЮ — алфавитный порядок)
    //   3. Третичный критерий: timestamp (по УБЫВАНИЮ — новые записи первыми)
    //
    // Зачем многоуровневая сортировка?
    //   - Один score недостаточен: два игрока с одинаковым счётом потеряют одну запись в TreeSet.
    //   - Два уровня (score + name) недостаточны: один игрок с двумя одинаковыми рекордами потеряет второй.
    //   - Три уровня гарантируют: compareTo() == 0 только для ДЕЙСТВИТЕЛЬНО одинаковых записей.
    @Override
    public int compareTo(BattleRecord other) {
        // ===== Long.compare(a, b) — БЕЗОПАСНОЕ СРАВНЕНИЕ ЧИСЕЛ =====
        //
        // Long.compare(a, b) возвращает:
        //   отрицательное число, если a < b
        //   0,                   если a == b
        //   положительное число, если a > b
        //
        // ПОЧЕМУ НЕ ВЫЧИТАНИЕ? (other.score - this.score)
        // При вычитании двух long может произойти ПЕРЕПОЛНЕНИЕ (overflow):
        //   Long.MAX_VALUE - (-1) = Long.MAX_VALUE + 1 → отрицательное число! (неверный результат)
        //   Long.compare() использует условные операторы (< > ==), переполнение невозможно.
        //
        // ПОРЯДОК АРГУМЕНТОВ определяет направление сортировки:
        //   Long.compare(this.score, other.score)  → ВОЗРАСТАНИЕ (обычный порядок: меньшее → большее)
        //   Long.compare(other.score, this.score)  → УБЫВАНИЕ (обратный порядок: большее → меньшее)
        //
        // Мы используем other.score первым → УБЫВАНИЕ → лучший score наверху таблицы.
        int scoreCompare = Long.compare(other.score, this.score); // убывание!

        // Если score различаются — результат определён, дальше сравнивать не нужно.
        if (scoreCompare != 0) {
            return scoreCompare;
        }

        // ===== ВТОРИЧНАЯ СОРТИРОВКА: по имени героя (алфавитный порядок, возрастание) =====
        //
        // String.compareTo() — лексикографическое сравнение строк (посимвольно).
        // Сравнивает символы по их Unicode-кодам:
        //   "А".compareTo("Б") < 0  (А идёт раньше Б)
        //   "Маг".compareTo("Маг") == 0  (строки равны)
        //   "Яна".compareTo("Анна") > 0  (Я идёт после А)
        //
        // this.heroName.compareTo(other.heroName) → ВОЗРАСТАНИЕ (А → Я).
        int nameCompare = this.heroName.compareTo(other.heroName);
        if (nameCompare != 0) {
            return nameCompare;
        }

        // ===== ТРЕТИЧНАЯ СОРТИРОВКА: по времени (новые первыми, убывание) =====
        //
        // Это КРИТИЧЕСКИ ВАЖНО для TreeSet!
        //
        // Без третьего уровня: если один игрок "Воин" набирает 1000 очков дважды,
        // обе записи имеют одинаковый score и heroName → compareTo() == 0.
        // TreeSet решит: это дубликат, и НЕ добавит вторую запись!
        //
        // С третьим уровнем: timestamp различается → compareTo() != 0 → обе записи сохранятся.
        // other.timestamp первым → УБЫВАНИЕ → более свежая запись выше.
        return Long.compare(other.timestamp, this.timestamp);
    }
}
