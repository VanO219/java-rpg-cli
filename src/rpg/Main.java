// ===== ТОЧКА ВХОДА В JAVA-ПРОГРАММУ =====
//
// Каждая Java-программа начинает выполнение с метода main().
// JVM (Java Virtual Machine, виртуальная машина Java) — среда выполнения байт-кода.
// При запуске команда: java rpg.Main
//   1. JVM загружает класс Main из скомпилированного .class-файла.
//   2. JVM ищет в нём метод с точной сигнатурой: public static void main(String[] args).
//   3. Если находит — запускает. Если нет — ошибка "Main method not found".
//
// Файл Main.java — минималистичная точка входа.
// Вся игровая логика находится в классе Game. Это принцип единой ответственности:
// Main знает только КАК запустить Game, а Game знает КАК играть.
//
// package (пакет) — группирует связанные классы в одно пространство имён.
// Имя пакета соответствует структуре папок: пакет rpg → папка src/rpg/.
// Все файлы с package rpg; видят друг друга без дополнительных импортов.
package rpg;

// ===== КЛАСС Main — ОБОЛОЧКА ТОЧКИ ВХОДА =====
//
// В Java нельзя объявить метод вне класса — ВСЁ должно быть внутри класса.
// В отличие от C/C++, где main() — глобальная функция, в Java это обычный static-метод.
//
// Main — это просто «обёртка» для метода main(). Класс сам по себе не создаётся.
// В других языках (Kotlin, Scala) точка входа может быть функцией без класса-обёртки.
//
// public class — объявление класса с модификатором доступа public.
// public — класс доступен из любого другого пакета (без public — только из своего пакета).
// class — основная единица кода в Java. Всё (данные и логика) живёт внутри классов.
// Имя класса ДОЛЖНО совпадать с именем файла: класс Main → файл Main.java.
// Одно публичное (public) объявление на файл — это правило Java.
public class Main {

    // ===== СИГНАТУРА ТОЧКИ ВХОДА =====
    //
    // public static void main(String[] args) — единственно допустимая сигнатура.
    // Если изменить ЛЮБУЮ часть — JVM не найдёт точку входа:
    //   × private static void main(...)  ← JVM не имеет доступа
    //   × public void main(...)          ← нет static, нельзя вызвать без объекта
    //   × public static int main(...)    ← не void, JVM ожидает void
    //   × public static void main()     ← нет String[] args
    //
    // public — метод доступен извне (JVM должна его вызвать из внешнего кода загрузчика).
    // static — метод принадлежит КЛАССУ, а не объекту. JVM вызывает Main.main()
    //          без создания экземпляра Main (не нужен new Main()).
    //          Это необходимо: ведь объект Main никто не создавал!
    // void — метод ничего не возвращает JVM. Для кода завершения используют System.exit(code).
    // String[] args — массив строк, аргументы командной строки.
    //   String[] — массив строк (каждый элемент — отдельная строка).
    //   Пример: java rpg.Main hero wizard → args[0]="hero", args[1]="wizard".
    //   Если аргументов нет — args — пустой массив (длина 0), но НЕ null.
    //   Можно безопасно проверить: if (args.length > 0) { ... }
    public static void main(String[] args) {

        // new Game() — создание нового объекта (экземпляра) класса Game.
        // new — оператор, который:
        //   1. Выделяет память в куче (heap) для нового объекта.
        //   2. Вызывает конструктор Game() для инициализации полей.
        //   3. Возвращает ссылку на созданный объект.
        //
        // Game game — переменная типа Game.
        // Важно: game — это не сам объект, а ССЫЛКА на него.
        // В Java все объекты живут в куче (heap), переменные хранят только адрес (ссылку).
        // Если написать Game game2 = game; — оба указывают на ОДИН объект.
        Game game = new Game();

        // game.start() — вызов метода start() у объекта game.
        // Точка (.) — оператор доступа к членам объекта (полям и методам).
        // Метод start() содержит всю игровую логику: меню, создание героя, бои, статистику.
        // После возврата из start() программа завершается (Main.main() заканчивается).
        game.start();
    }
}
