    // ===== АСИММЕТРИЧНАЯ МОДЕЛЬ УРОНА (дизайнерское решение) =====
    //
    // У врагов НЕТ поля defense — урон применяется напрямую: health -= damage.
    // У героя (GameCharacter) урон УМЕНЬШАЕТСЯ на defense: health -= max(1, damage - defense).
    //
    // Это ОСОЗНАННОЕ упрощение для учебного проекта:
    //   - Герои имеют сложную механику (защита, уклонение, способности).
    //   - Враги — простые «мишени» с базовыми HP и атакой.
    //   - Добавление defense к врагам усложнило бы балансировку без учебной пользы.
    //
    // В реальной RPG обе стороны обычно имеют одинаковую формулу урона.
    // Студентам: если хотите добавить defense врагу — нужно добавить поле,
    // параметр в конструктор, и формулу Math.max(1, damage - defense) как у героя.
    /**
     * Наносит урон врагу: уменьшает здоровье на величину damage.
     * Здоровье не может стать отрицательным (ограничено нулём).
     * <p>
     * ОТЛИЧИЕ ОТ ГЕРОЯ: урон применяется без вычитания defense (у врагов нет защиты).
     *
     * @param damage количество урона (неотрицательное число)
     */
    public void takeDamage(int damage) {
        // Math — стандартный класс Java из пакета java.lang (импортировать не нужно).
        // Math.max(a, b) — СТАТИЧЕСКИЙ метод: возвращает большее из двух чисел.
        //
        // Статический метод (static) принадлежит КЛАССУ, а не объекту.
        // Вызывается через имя класса: Math.max(...), а НЕ через new Math().max(...).
        // Создавать объект Math нельзя — все его методы статические (утилитный класс).
        //
        // Math.max(0, health - damage) гарантирует, что здоровье не станет отрицательным.
        // Если health - damage < 0, результатом будет 0.
        // Пример: health = 30, damage = 50 → Math.max(0, 30 - 50) = Math.max(0, -20) = 0.
        health = Math.max(0, health - damage);
    }

    /**
     * Проверяет, жив ли враг (здоровье больше нуля).
     *
     * @return true, если здоровье > 0; false, если враг мёртв
     */
    public boolean isAlive() {
        // return — возвращает результат вызвавшему коду и завершает выполнение метода.
        // health > 0 — выражение сравнения, результат которого — boolean (true или false).
        return health > 0;
    }

