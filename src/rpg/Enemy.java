    // ===== КЛОНИРОВАНИЕ — Cloneable + clone() =====
    //
    // clone() — метод класса Object, создающий КОПИЮ объекта.
    //
    // Как работает clone():
    //   1. super.clone() выделяет память под новый объект того же класса.
    //   2. Копирует значения ВСЕХ полей из оригинала в копию (побитовое копирование).
    //   3. Возвращает новый объект.
    //
    // ===== ПОВЕРХНОСТНАЯ КОПИЯ (shallow copy) =====
    //   - Примитивные поля (int, boolean) копируются по ЗНАЧЕНИЮ → изменение копии
    //     не влияет на оригинал.
    //   - Ссылочные поля (String, массивы, объекты) копируются по ССЫЛКЕ → и оригинал,
    //     и копия указывают на ОДИН И ТОТ ЖЕ объект в памяти.
    //   - Для String это безопасно: String неизменяем (immutable), общая ссылка не создаёт проблем.
    //   - Для EnemyRank (enum) тоже безопасно: enum — неизменяемый singleton.
    //   - Для изменяемых объектов (ArrayList, массивы) это ОПАСНО: изменение через копию
    //     повлияет на оригинал!
    //
    // ===== ГЛУБОКАЯ КОПИЯ (deep copy) =====
    //   Для глубокого копирования нужно вручную копировать каждый ссылочный объект:
    //     clone.someList = new ArrayList<>(this.someList);
    //   В этом классе все ссылочные поля неизменяемы (String, enum), поэтому
    //   поверхностной копии достаточно.
    //
    // ===== CloneNotSupportedException =====
    //   ПРОВЕРЯЕМОЕ исключение (checked exception).
    //   Бросается, если класс НЕ реализует Cloneable. Компилятор ТРЕБУЕТ его обработать
    //   (через try-catch или throws в сигнатуре метода).
    //   Так как наш класс реализует Cloneable, исключение никогда не произойдёт,
    //   но компилятор этого не знает — try-catch обязателен.
    //
    // ===== Ковариантный тип возврата (covariant return type) =====
    //   Метод clone() в Object возвращает Object.
    //   Мы меняем тип возврата на Enemy — это РАЗРЕШЕНО (Java 5+):
    //   переопределённый метод может возвращать ПОДТИП оригинального типа возврата.
    //   Это удобно: вызывающий код получает Enemy, а не Object (не нужно приведение типа).
    //   Пример: Enemy clone = enemy.clone(); — без (Enemy) приведения.
    //
    // ===== Модификатор доступа при переопределении =====
    //   clone() в Object — protected. Мы меняем на public.
    //   При переопределении модификатор доступа можно РАСШИРИТЬ (protected → public),
    //   но НЕЛЬЗЯ сузить (public → private — ошибка компиляции).
    //
    // Современные АЛЬТЕРНАТИВЫ clone():
    //   - Копирующий конструктор: public Enemy(Enemy other) { ... }
    //   - Статический фабричный метод: Enemy.createVariant(template, name, rank)
    //   clone() считается «сломанным» в Java (см. Effective Java, Item 13):
    //     — требует Cloneable, иначе бросает исключение;
    //     — нарушает контракт final-полей (нельзя изменить после super.clone());
    //     — сложно правильно реализовать для наследников.
    //   Но clone() полезен для изучения механизма клонирования.
    //
    // @Override — переопределяем метод clone() из класса Object.
    // (см. подробное объяснение @Override ниже в toString())

    /**
     * Создаёт КОПИЮ врага (поверхностное клонирование).
     * Все примитивные поля копируются по значению.
     * Ссылочные поля (String, EnemyRank) — неизменяемые, поэтому shallow copy безопасен.
     *
     * @return новый объект Enemy с теми же характеристиками
     */
    @Override
    public Enemy clone() {
        try {
            // super.clone() — вызов метода clone() класса Object.
            // Создаёт поверхностную копию объекта: новая память, скопированные поля.
            // (Enemy) — приведение типа (cast): Object → Enemy.
            // super.clone() возвращает Object, но мы ЗНАЕМ, что это Enemy
            // (ковариантный тип возврата позволяет вернуть Enemy вместо Object).
            return (Enemy) super.clone();
        } catch (CloneNotSupportedException e) {
            // Этот catch НИКОГДА не сработает, потому что класс реализует Cloneable.
            // Но компилятор требует обработать checked exception — он не анализирует
            // implements Cloneable, а просто видит throws CloneNotSupportedException у super.clone().
            // AssertionError — наследник Error (НЕ Exception), означает:
            //   «Программа в невозможном состоянии — продолжать нельзя».
            throw new AssertionError("Клонирование не поддерживается", e);
        }
    }

    // ===== СТАТИЧЕСКИЙ ФАБРИЧНЫЙ МЕТОД (static factory method) =====
    //
    // Статический фабричный метод — альтернатива конструктору для создания объектов.
    // Вызывается через имя класса: Enemy.createVariant(template, "Элитный Гоблин", EnemyRank.ELITE).
    //
    // Преимущества перед конструктором:
    //   1. Имеет ИМЯ — сразу понятно, что создаётся (createVariant, of, from, valueOf).
    //      Конструктор всегда называется Enemy() — из имени не ясно, что это «вариант шаблона».
    //   2. Может возвращать подтип (наследника) или кэшированный объект.
    //   3. Может вернуть null или Optional (конструктор — только объект или исключение).
    //   4. Не обязан каждый раз создавать НОВЫЙ объект (может вернуть кэшированный).
    //
    // Здесь: создаёт нового врага на основе существующего (шаблона), но с другим именем и рангом.
    // Используется как альтернатива clone() — более гибкий и понятный подход.

    /**
     * Создаёт нового врага на основе шаблона с другим именем и рангом.
     * Берёт базовые характеристики (здоровье, атаку, опыт) из шаблона.
     *
     * @param template враг-шаблон (источник базовых характеристик)
     * @param newName  имя нового врага
     * @param newRank  ранг нового врага (влияет на характеристики через множитель)
     * @return новый объект Enemy с характеристиками, скорректированными по рангу
     */
    public static Enemy createVariant(Enemy template, String newName, EnemyRank newRank) {
        // Используем БАЗОВЫЕ характеристики шаблона (baseHealth, baseAttack) — значения
        // ДО применения множителя ранга. Конструктор сам применит множитель newRank.
        //
        // Почему НЕ getHealth()/getAttack()?
        // getHealth() возвращает МОДИФИЦИРОВАННОЕ значение (base * множитель_старого_ранга).
        // Если передать его в конструктор, множитель нового ранга применится ПОВТОРНО:
        //   ELITE → BOSS: 30 * 1.5 = 45 → 45 * 2.5 = 112 (двойное применение — БАГ!)
        //   Правильно:    30 * 2.5 = 75 (один множитель — ВЕРНО)
        //
        // template.baseHealth — прямой доступ к private-полю ДРУГОГО объекта того же класса.
        // Это РАЗРЕШЕНО в Java: модификатор private ограничивает доступ на уровне КЛАССА,
        // а не объекта. Любой код внутри класса Enemy может читать private-поля
        // ЛЮБОГО объекта Enemy (не только this).
        return new Enemy(newName, template.baseHealth, template.baseAttack,
                template.getExpReward(), newRank);
    }

    // ===== getSpeed() — СКОРОСТЬ ВРАГА (для PriorityQueue, глава 5.7) =====
    //
    // Скорость определяет порядок хода в бою.
    // Формула: базовая (5) + attack / 3 + бонус от ранга.
    // Боссы немного быстрее обычных врагов благодаря множителю ранга.

    /**
     * Возвращает скорость врага (для определения порядка ходов в бою).
     * Формула: 5 + attack/3 + (rank.multiplier * 2).
     *
     * @return скорость врага (чем больше — тем раньше ходит)
     */
    public int getSpeed() {
        // Тернарный оператор: если rank не null → вычисляем бонус, иначе 0.
        // (int) — приведение double к int (отбрасывает дробную часть, НЕ округляет).
        // rank.getMultiplier() возвращает double → * 2 → int (усечение).
        int rankBonus = (rank != null) ? (int) (rank.getMultiplier() * 2) : 0;
        // attack / 3 — целочисленное деление (int / int = int): 10 / 3 = 3, а не 3.33.
        return 5 + attack / 3 + rankBonus;
    }

    // ===== compareTo() — РЕАЛИЗАЦИЯ Comparable<Enemy> (глава 5.5) =====
    //
    // compareTo() — единственный метод интерфейса Comparable<T>.
    // Определяет «ЕСТЕСТВЕННЫЙ ПОРЯДОК» объектов для сортировки.
    //
    // Контракт compareTo():
    //   Возвращает:
    //     < 0 — этот объект МЕНЬШЕ other (идёт раньше при сортировке).
    //     = 0 — объекты равны по порядку.
    //     > 0 — этот объект БОЛЬШЕ other (идёт позже при сортировке).
    //
    //   Математические свойства (контракт):
    //     - Антисимметрия: если a.compareTo(b) > 0, то b.compareTo(a) < 0.
    //     - Транзитивность: если a > b и b > c, то a > c.
    //     - Согласованность: если a.compareTo(b) == 0, то sign(a.compareTo(c)) == sign(b.compareTo(c)).
    //   Нарушение контракта → непредсказуемое поведение сортировки!
    //
    // ===== Integer.compare(a, b) — БЕЗОПАСНОЕ СРАВНЕНИЕ =====
    //
    // НИКОГДА не используй return a - b для сравнения int!
    //   Проблема: при больших числах может быть ПЕРЕПОЛНЕНИЕ (overflow).
    //   Пример: Integer.MAX_VALUE - (-1) = отрицательное число (переполнение)!
    //   Это даст НЕВЕРНЫЙ результат сортировки.
    //
    // Integer.compare(a, b) — безопасная альтернатива:
    //   Возвращает -1, 0 или 1. Нет риска переполнения.
    //   Всегда используй Integer.compare() вместо a - b.

    /**
     * Сравнивает врагов по суммарной мощи (health + attack).
     * Используется для естественной сортировки: слабые враги первыми, сильные — последними.
     *
     * <p>Примеры использования:
     * <pre>
     *   Collections.sort(enemies);          // сортировка по мощи (этот метод)
     *   enemies.sort(null);                  // эквивалент — null означает natural ordering
     *   TreeSet&lt;Enemy&gt; sorted = new TreeSet&lt;&gt;(enemies);  // автоматическая сортировка
     * </pre>
     *
     * @param other другой враг для сравнения
     * @return отрицательное число, 0 или положительное число
     */
    @Override
    public int compareTo(Enemy other) {
        // Суммарная мощь = текущее здоровье + текущая атака.
        // Используем health (с учётом ранга), а НЕ baseHealth (без ранга):
        // сортировка по «боевой» мощи важнее, чем по базовым характеристикам.
        // Побочный эффект: мощь раненого врага (с уменьшенным health) ниже, чем свежего — это нормально.
        int thisPower = this.health + this.attack;
        int otherPower = other.health + other.attack;
        // Integer.compare — безопасное сравнение без переполнения.
        // Возвращает: -1 (thisPower < otherPower), 0 (равны), 1 (thisPower > otherPower).
        return Integer.compare(thisPower, otherPower);
    }

    // ===== ПЕРЕОПРЕДЕЛЕНИЕ equals() и hashCode() =====
    //
    // equals() и hashCode() — два метода класса Object, которые ВСЕГДА переопределяются ВМЕСТЕ.
    //
    // ===== Контракт equals() =====
    //   - Рефлексивность: a.equals(a) == true.
    //   - Симметрия: если a.equals(b), то b.equals(a).
    //   - Транзитивность: если a.equals(b) и b.equals(c), то a.equals(c).
    //   - Консистентность: повторные вызовы дают тот же результат (если объект не менялся).
    //   - a.equals(null) == false всегда.
    //
    // ===== Контракт hashCode() =====
    //   - Если a.equals(b) == true, то a.hashCode() == b.hashCode() (ОБЯЗАТЕЛЬНО).
    //   - Если a.equals(b) == false, hashCode МОЖЕТ совпадать (коллизия), но лучше — нет.
    //   - Нарушение: если equals() использует одни поля, а hashCode() — другие,
    //     объект «потеряется» в HashMap/HashSet (положили по одному хешу, ищем по другому).
    //
    // ===== Comparable и equals — СОГЛАСОВАННОСТЬ =====
    //
    // Рекомендация из Javadoc Comparable: «natural ordering should be consistent with equals».
    // Это значит: (a.compareTo(b) == 0) должно совпадать с a.equals(b).
    //
    // У класса Enemy это НАМЕРЕННО НЕ ТАК:
    //   - compareTo() сравнивает по МОЩИ (health + attack) — это порядок СИЛЫ.
    //   - equals() сравнивает по ИДЕНТИЧНОСТИ (name + rank) — это «кто этот враг».
    //
    // Два гоблина с одинаковой мощью НЕ равны (разные имена/ранги).
    // Два врага с именем "Гоблин" и рангом COMMON РАВНЫ, даже если у одного
    // изменилось здоровье в бою (потерял HP).
    //
    // Это несоответствие ДОПУСТИМО, но имеет ПОСЛЕДСТВИЕ:
    //   TreeSet использует compareTo() (не equals()) для определения уникальности.
    //   Если два Enemy имеют одинаковую мощь, TreeSet считает их «дубликатами»
    //   и оставит только одного — даже если equals() говорит, что они РАЗНЫЕ.
    //   Решение: в TreeSet передавать Comparator, который различает таких врагов.

    /**
     * Сравнивает врагов по идентичности: имя + ранг.
     * Два врага считаются равными, если у них одинаковое имя и одинаковый ранг,
     * независимо от текущего здоровья (которое может меняться в бою).
     *
     * @param obj объект для сравнения
     * @return true, если враги идентичны по имени и рангу
     */
    @Override
    public boolean equals(Object obj) {
        // 1. Проверка на ту же ссылку (оптимизация): если obj — тот же объект, точно равны.
        //    this == obj сравнивает АДРЕСА в памяти, а не содержимое.
        if (this == obj) return true;

        // 2. Проверка на null и на тип.
        //    instanceof — оператор, проверяющий: «является ли объект экземпляром данного класса?»
        //    null instanceof Enemy → false (null не является экземпляром ничего).
        //    Поэтому отдельная проверка на null не нужна — instanceof её включает.
        //
        //    «Pattern matching instanceof» (Java 16+): если проверка пройдена,
        //    переменная enemy автоматически создаётся с нужным типом.
        //    Без pattern matching пришлось бы писать: Enemy enemy = (Enemy) obj;
        if (!(obj instanceof Enemy enemy)) return false;

        // 3. Сравнение значимых полей.
        //    name.equals() — сравнение строк по СОДЕРЖИМОМУ (не по ссылке).
        //    rank == enemy.rank — для enum сравнение по == корректно:
        //    каждое значение enum — singleton (единственный объект в памяти).
        return name.equals(enemy.name) && rank == enemy.rank;
    }

    /**
     * Возвращает хеш-код на основе имени и ранга.
     * ОБЯЗАТЕЛЬНО согласован с equals(): если equals() == true, hashCode() одинаков.
     *
     * @return целочисленный хеш-код объекта
     */
    @Override
    public int hashCode() {
        // java.util.Objects.hash() — полное имя класса используется без import-строки в начале файла.
        // В Java допустимо обращаться к любому классу через полное имя (qualified name).
        // Это удобно, когда импорт не добавлен или когда нужно явно показать, из какого пакета класс.
        //
        // Objects.hash() — утилитный метод из java.util.Objects.
        // Принимает произвольное количество аргументов (varargs) и вычисляет
        // комбинированный хеш-код по формуле: 31 * (31 * hash(name) + hash(rank)).
        //
        // Число 31 выбрано не случайно:
        //   - Нечётное простое число → хорошее распределение хешей.
        //   - 31 * x == (x << 5) - x → JVM оптимизирует умножение в битовый сдвиг.
        //
        // ВАЖНО: hashCode() использует ТЕ ЖЕ поля, что и equals() (name, rank).
        // Если добавить в equals() проверку health, но забыть в hashCode() —
        // объект может «потеряться» в HashMap (разные хеши для «равных» объектов).
        return java.util.Objects.hash(name, rank);
    }

    // ===== ПЕРЕОПРЕДЕЛЕНИЕ toString() =====

    // @Override — аннотация-маркер: сообщает компилятору, что этот метод
    // ПЕРЕОПРЕДЕЛЯЕТ (заменяет) метод из родительского класса (Object).
    // Если имя метода написано с ошибкой (например, tostring), компилятор выдаст ошибку.
    // Всегда ставь @Override — это защита от опечаток.
    //
    // Аннотация (@) — специальная метка в коде, которая даёт дополнительную информацию
    // компилятору, фреймворку или инструментам. Аннотации начинаются с символа @.
    // Аннотации НЕ меняют логику программы, но могут влиять на компиляцию и выполнение.
    //
    // toString() — метод класса Object. В Java КАЖДЫЙ класс неявно наследуется от Object.
    // Object — корневой класс иерархии, основные методы:
    //   toString() — текстовое представление объекта.
    //   equals(Object) — сравнение по содержимому (по умолчанию — по ссылке).
    //   hashCode() — целочисленный хеш объекта (для HashMap, HashSet).
    //   clone() — копирование объекта.
    //
    // По умолчанию toString() возвращает "rpg.Enemy@1a2b3c" (имя класса + хеш) — бесполезно.
    // Переопределяя toString(), мы задаём ЧИТАЕМОЕ текстовое представление объекта.
    //
    // Метод вызывается автоматически при:
    //   - конкатенации со строкой: "Враг: " + enemy
    //   - выводе в консоль: System.out.println(enemy)
    //   - в отладчике (debugger) при просмотре значений переменных

    /**
     * Возвращает читаемое строковое представление врага.
     * Формат: "ИмяСУчётомРанга [HP: здоровье ATK: атака]"
     *
     * @return строковое представление врага
     */
    @Override
    public String toString() {
        // rank.formatName(name) — метод enum EnemyRank, форматирующий имя с учётом ранга.
        // Например: COMMON → "Гоблин", ELITE → "Элитный Гоблин", BOSS → "★ Гоблин ★".
        String displayName = rank.formatName(name);

        // ===== StringBuilder (глава 7.3) =====
        //
        // StringBuilder — изменяемый буфер символов: не создаёт промежуточных строк
        // при сборке. Полное объяснение (immutability, capacity, method chaining,
        // append-overloads) — см. GameCharacter.java, метод toString().
        //
        // Здесь конструктор без аргументов: начальная ёмкость = 16 символов.
        // Этого хватит; при нехватке StringBuilder удваивает буфер автоматически.
        StringBuilder sb = new StringBuilder();

        // append(String) — добавляет строку (результат метода rank.formatName)
        sb.append(displayName)
          // append(String) — добавляет строковый литерал
          .append(" [HP: ")
          // append(int) — добавляет целое число, автоматически преобразуя в текст
          // (вызывается Integer.toString(health) внутри StringBuilder)
          .append(health)
          .append(" ATK: ")
          // append(int) — ещё одно целое число
          .append(attack)
          // append(char) — добавляет один символ (экономичнее, чем append("]"))
          .append(']');

        // toString() — собирает финальную строку из внутреннего буфера StringBuilder.
        // Результат идентичен прежней конкатенации: "Элитный Гоблин [HP: 150 ATK: 30]"
        return sb.toString();
    }
