// Пакет rpg — все классы нашей RPG-игры (см. подробное объяснение в DamageType.java).
package rpg;

// ===== enum ДЛЯ СИСТЕМЫ ДОСТИЖЕНИЙ (глава 5.4, 3.8) =====
//
// Achievement — перечисление всех возможных достижений в игре.
// Достижения — это уникальные награды за определённые действия (первая победа, бой без урона и т.д.).
//
// ===== ПОЧЕМУ enum, А НЕ String ИЛИ int? =====
//
// Способ 1 — int-константы (плохо):
//   static final int FIRST_BLOOD = 0;
//   static final int FLAWLESS = 1;
//   Проблемы:
//     - Нет типобезопасности: метод unlock(int id) примет ЛЮБОЕ число (unlock(999) — ошибки нет).
//     - Нет данных: нельзя привязать описание и иконку к числу без дополнительной Map.
//     - Легко перепутать: число 1 — это FLAWLESS или COLLECTOR?
//
// Способ 2 — String-константы (лучше, но всё ещё плохо):
//   static final String FIRST_BLOOD = "FIRST_BLOOD";
//   Проблемы:
//     - Опечатки: unlock("FRIST_BLOOD") — компилятор не заметит ошибку.
//     - Нет автодополнения в IDE: нужно помнить все строки наизусть.
//     - Невозможен switch с проверкой полноты (компилятор не знает все значения).
//
// Способ 3 — enum (правильно):
//   Achievement.FIRST_BLOOD — типобезопасно, с автодополнением, с данными.
//   unlock(Achievement.INVALID) — ошибка компиляции, INVALID не существует.
//   Каждая константа хранит описание и иконку (поля enum).
//
// ===== enum И HashSet — ПОЧЕМУ ОНИ ИДЕАЛЬНО РАБОТАЮТ ВМЕСТЕ (глава 5.4) =====
//
// HashSet<Achievement> — множество достижений, гарантирующее УНИКАЛЬНОСТЬ.
// При вызове achievements.add(Achievement.FIRST_BLOOD) повторно — элемент НЕ добавится.
//
// КАК HashSet ГАРАНТИРУЕТ УНИКАЛЬНОСТЬ:
//
// HashSet использует хеш-таблицу (массив «корзин», buckets).
// При добавлении элемента:
//   1. Вызывается element.hashCode() → число (хеш-код).
//   2. По хеш-коду определяется номер корзины: bucket = hashCode % capacity.
//   3. В корзине ищется элемент через equals():
//      - Если найден equal элемент → элемент НЕ добавляется (дубликат).
//      - Если не найден → элемент добавляется в корзину.
//
// Для enum ВСЕ ЭТО РАБОТАЕТ АВТОМАТИЧЕСКИ:
//   - hashCode() — унаследован от java.lang.Enum, основан на ordinal() (порядковом номере).
//   - equals() — сравнивает через == (у каждой константы ровно ОДИН экземпляр в JVM).
//   - Это делает enum идеальным типом для HashSet: быстро, безопасно, без ошибок.
//
// Сложность операций HashSet:
//   add(), remove(), contains() — O(1) в среднем (мгновенно).
//   В худшем случае (много коллизий) — O(n), но для enum коллизий практически не бывает.
//
// СРАВНЕНИЕ Set-реализаций:
//   HashSet    — O(1) доступ, порядок НЕ гарантирован.
//   TreeSet    — O(log n) доступ, элементы ОТСОРТИРОВАНЫ.
//   LinkedHashSet — O(1) доступ, сохраняет ПОРЯДОК ВСТАВКИ.
//   EnumSet    — специальный Set для enum, САМЫЙ БЫСТРЫЙ (битовая маска внутри).
//
// Для достижений подходит любой Set, но HashSet — универсальный выбор.
// EnumSet.noneOf(Achievement.class) был бы оптимальнее, но HashSet проще для обучения.
public enum Achievement {

    // ===== КОНСТАНТЫ ENUM — КАЖДАЯ ЭТО ОБЪЕКТ С ПОЛЯМИ =====
    //
    // Каждая константа enum — это экземпляр класса Achievement.
    // В скобках передаются аргументы конструктору: (description, icon).
    // Константы разделяются запятой, после последней — точка с запятой.
    //
    // Внутри JVM каждая константа — это:
    //   public static final Achievement FIRST_BLOOD = new Achievement("Первая кровь...", "🗡");
    // Но конструктор вызывается ОДИН раз при загрузке класса, и вызвать его извне НЕЛЬЗЯ.

    // Первая победа над врагом.
    FIRST_BLOOD("Первая кровь — одержите первую победу", "🗡"),

    // Победить врага, не получив урона.
    FLAWLESS("Без царапины — победить врага без урона", "🛡"),

    // Собрать 3+ предмета из лута.
    COLLECTOR("Коллекционер — собрать 3 или более предметов", "🎒"),

    // Победить босса (враг ранга BOSS).
    DRAGON_SLAYER("Убийца дракона — победить босса", "🐉"),

    // Победить всех 5 врагов.
    FULL_CLEAR("Все враги повержены — победить всех врагов", "⭐"),

    // Выжить с 1 HP или менее 10% здоровья.
    SURVIVOR("Выжил чудом — выжить с менее чем 10% HP", "💀"),

    // Нанести 50+ урона за один удар.
    DEVASTATOR("Разрушитель — нанести 50+ урона за один удар", "💥"),

    // Использовать спецатаку 5+ раз.
    SPECIALIST("Специалист — использовать 5+ спецатак", "✨");

    // ===== ПОЛЯ ENUM =====
    //
    // private — доступны только внутри этого enum (инкапсуляция).
    // final — значения устанавливаются в конструкторе и больше НЕ меняются.
    // Каждая константа (FIRST_BLOOD, FLAWLESS и т.д.) имеет СВОИ значения полей.

    // Текстовое описание достижения на русском языке.
    // String — ссылочный тип (класс), хранит строку в куче (heap).
    private final String description;

    // Иконка достижения — Unicode-символ (эмодзи).
    // Одна строка может содержать эмодзи, т.к. Java использует UTF-16 для хранения символов.
    private final String icon;

    // ===== КОНСТРУКТОР ENUM =====
    //
    // Конструктор enum — ВСЕГДА приватный (private), даже если не написать private явно.
    // Это важное отличие от обычного класса!
    //
    // Почему приватный?
    //   - Константы enum создаются ТОЛЬКО при загрузке класса (class loading).
    //   - new Achievement("...", "...") извне — ОШИБКА КОМПИЛЯЦИИ.
    //   - Это гарантирует фиксированный набор значений: ровно 8 достижений, не больше.
    //
    // Конструктор вызывается автоматически для каждой константы:
    //   FIRST_BLOOD("Первая кровь...", "🗡") → Achievement("Первая кровь...", "🗡")
    Achievement(String description, String icon) {
        this.description = description;
        this.icon = icon;
    }

    // ===== ГЕТТЕРЫ (методы доступа к полям) =====
    //
    // Поля private — напрямую к ним обратиться нельзя.
    // Геттеры предоставляют контролируемый доступ на чтение.
    // В обычных классах принято писать getDescription(), но в enum оба варианта допустимы.

    /**
     * Возвращает описание достижения на русском языке.
     *
     * @return строка с описанием, например "Первая кровь — одержите первую победу"
     */
    public String getDescription() {
        return description;
    }

    /**
     * Возвращает иконку достижения (Unicode-символ / эмодзи).
     *
     * @return строка с иконкой, например "🗡"
     */
    public String getIcon() {
        return icon;
    }

    // ===== toString() — СТРОКОВОЕ ПРЕДСТАВЛЕНИЕ ОБЪЕКТА =====
    //
    // @Override — аннотация, указывающая что метод переопределяет родительский
    // (см. подробное объяснение в GameCharacter.java).
    //
    // toString() — метод класса Object, который есть у КАЖДОГО объекта в Java.
    // По умолчанию для enum toString() возвращает имя константы: "FIRST_BLOOD".
    // Мы переопределяем, чтобы при выводе показывалось описание с иконкой.
    //
    // toString() вызывается автоматически при:
    //   System.out.println(achievement);          // → "🗡 Первая кровь — одержите первую победу"
    //   "Достижение: " + achievement;             // → "Достижение: 🗡 Первая кровь..."
    //   String.valueOf(achievement);               // → "🗡 Первая кровь..."
    //
    // ПОЛЕЗНЫЕ МЕТОДЫ ENUM (унаследованы от java.lang.Enum):
    //   name()    — имя константы как строка: "FIRST_BLOOD" (всегда совпадает с объявлением).
    //   ordinal() — порядковый номер: FIRST_BLOOD=0, FLAWLESS=1, COLLECTOR=2, ...
    //   values()  — статический метод, возвращает массив ВСЕХ констант: Achievement[].
    //   valueOf("FIRST_BLOOD") — возвращает константу по имени (IllegalArgumentException если нет).
    @Override
    public String toString() {
        return icon + " " + description;
    }
}
