// Пакет rpg — все классы нашей RPG-игры (см. подробное объяснение в DamageType.java).
package rpg;

// ===== enum С ЧИСЛОВЫМИ ПОЛЯМИ ДЛЯ ИГРОВЫХ РАСЧЁТОВ =====
//
// Этот enum демонстрирует использование enum для хранения игровых параметров (баланса).
//
// В RPG-играх враги часто имеют ранги: обычные, элитные, боссы.
// Ранг определяет множитель характеристик: элитный враг имеет в 1.5 раза больше
// здоровья и урона, босс — в 2.5 раза.
//
// Почему enum, а не Map или конфиг?
//   1. Типобезопасность: нельзя передать несуществующий ранг.
//   2. Всё в одном месте: имя, множитель, иконка — рядом.
//   3. Методы: applyStat() и formatName() — прямо в enum.
//   4. Неизменяемость: значения нельзя случайно изменить в рантайме.
//
// enum может хранить любые типы данных в полях: String, int, double, другие enum и т.д.
// Количество полей не ограничено — можно хранить сколько угодно характеристик.
//
// ===== enum И ИНТЕРФЕЙСЫ =====
//
// enum может реализовать один или несколько интерфейсов (implements).
// Например:
//   public enum EnemyRank implements Comparable<EnemyRank>, Serializable { ... }
//
// Comparable<EnemyRank> уже реализован автоматически (enum наследует Enum,
// который реализует Comparable). Порядок сравнения — по ordinal() (порядку объявления).
// Поэтому COMMON < ELITE < BOSS при сравнении через compareTo().
//
// А вот наследовать класс (extends) enum НЕ МОЖЕТ — он уже наследует java.lang.Enum.
//
// EnemyRank описывает ранги врагов в RPG: обычный, элитный, босс.
public enum EnemyRank {

    // ===== КОНСТАНТЫ enum =====
    //
    // Три параметра в конструкторе: displayName, multiplier, icon.
    // Каждая константа передаёт свои значения в конструктор.

    // Обычный враг — базовые характеристики (множитель 1.0), без иконки.
    // 1.0 — литерал типа double (число с плавающей точкой двойной точности).
    // "" — пустая строка (String длиной 0 символов).
    COMMON("Обычный", 1.0, ""),

    // Элитный враг — усиленные характеристики (в 1.5 раза сильнее обычного).
    // "★" — Unicode-символ звезды, используется как визуальный маркер в консоли.
    // Java поддерживает Unicode — можно использовать любые символы из таблицы Unicode.
    ELITE("Элитный", 1.5, "★"),

    // Босс — значительно усиленный враг (в 2.5 раза сильнее обычного).
    // "♛" — Unicode-символ короны, обозначает особо опасного врага.
    BOSS("Босс", 2.5, "♛");

    // ===== ПОЛЯ enum =====
    //
    // private final — приватные неизменяемые поля (см. подробное объяснение в GameState.java).

    // Отображаемое имя ранга на русском языке.
    private final String displayName;

    // Множитель характеристик (stat multiplier).
    // double — примитивный тип для чисел с плавающей точкой (дробных чисел).
    // double занимает 64 бита и обеспечивает точность ~15-17 значащих цифр.
    // Примеры: 1.0, 1.5, 2.5, 3.14159.
    //
    // Множитель применяется к базовым характеристикам врага (здоровье, атака, защита).
    // Обычный враг (1.0): 100 HP → 100 HP. Босс (2.5): 100 HP → 250 HP.
    private final double multiplier;

    // Иконка ранга для отображения в консоли.
    // Пустая строка "" для обычных врагов (без иконки), "★" для элитных, "♛" для боссов.
    private final String icon;

    // ===== КОНСТРУКТОР =====
    //
    // Конструктор enum всегда приватный (см. подробное объяснение в GameState.java).
    // Три параметра — по числу полей.
    EnemyRank(String displayName, double multiplier, String icon) {
        this.displayName = displayName;
        this.multiplier = multiplier;
        this.icon = icon;
    }

    // ===== ГЕТТЕРЫ =====

    /**
     * Возвращает отображаемое имя ранга.
     *
     * @return строка с названием ранга на русском языке
     */
    public String getDisplayName() {
        return displayName;
    }

    /**
     * Возвращает множитель характеристик для данного ранга.
     *
     * @return множитель (1.0 для обычного, 1.5 для элитного, 2.5 для босса)
     */
    public double getMultiplier() {
        return multiplier;
    }

    /**
     * Возвращает иконку ранга для отображения в консоли.
     *
     * @return строка с Unicode-иконкой (пустая строка для обычного ранга)
     */
    public String getIcon() {
        return icon;
    }

    // ===== МЕТОДЫ С ИГРОВОЙ ЛОГИКОЙ =====
    //
    // В enum можно размещать методы с произвольной логикой.
    // Это удобнее, чем выносить расчёты в отдельный утилитный класс,
    // потому что логика находится рядом с данными (принцип инкапсуляции).

    /**
     * Применяет множитель ранга к базовой характеристике.
     * Используется для масштабирования здоровья, атаки и защиты врага.
     *
     * Пример: ELITE.applyStat(100) → 150 (100 * 1.5 = 150).
     *
     * @param baseStat базовое значение характеристики
     * @return масштабированное значение (целое число)
     */
    public int applyStat(int baseStat) {
        // (int) — явное приведение типа (casting) из double в int.
        //
        // baseStat * multiplier: int * double = double (Java автоматически расширяет int до double).
        // Но результат должен быть int (характеристики в игре — целые числа).
        // (int) отбрасывает дробную часть: (int) 150.7 → 150 (НЕ округляет, а обрезает!).
        //
        // Если нужно округление — используют Math.round():
        //   (int) Math.round(baseStat * multiplier)
        // Но для игровых расчётов отбрасывание дробной части — стандартная практика.
        return (int) (baseStat * multiplier);
    }

    /**
     * Форматирует имя врага с учётом ранга.
     * Обычный враг: "Гоблин" (без изменений).
     * Элитный враг: "★ Гоблин (Элитный)".
     * Босс:         "♛ Гоблин (Босс)".
     *
     * @param baseName базовое имя врага (например, "Гоблин")
     * @return отформатированное имя с иконкой и рангом
     */
    public String formatName(String baseName) {
        // String.isEmpty() — проверяет, является ли строка пустой (длина == 0).
        // Для обычных врагов (icon = "") возвращаем имя как есть.
        //
        // Тернарный оператор (? :) — сокращённая форма if-else для выражений:
        //   условие ? значение_если_true : значение_если_false
        // Здесь: icon.isEmpty() ? baseName : icon + " " + baseName + " (" + displayName + ")"
        //
        // Если иконка пустая → просто имя. Иначе → иконка + имя + ранг в скобках.
        return icon.isEmpty() ? baseName : icon + " " + baseName + " (" + displayName + ")";
    }

    // ===== ПЕРЕОПРЕДЕЛЕНИЕ toString() =====
    //
    // По умолчанию toString() у enum возвращает name() — имя константы:
    //   EnemyRank.BOSS.toString() → "BOSS"
    //
    // Мы переопределяем, чтобы возвращать отображаемое имя:
    //   EnemyRank.BOSS.toString() → "Босс"
    //
    // toString() вызывается автоматически при конкатенации со строкой:
    //   "Ранг: " + EnemyRank.BOSS → "Ранг: Босс"
    //
    // ВАЖНО: переопределение toString() НЕ влияет на name() и valueOf().
    // name() всегда возвращает имя константы ("BOSS"), valueOf("BOSS") находит по имени.
    // toString() — только для отображения.

    /**
     * Возвращает отображаемое имя ранга.
     * Переопределяет стандартный toString(), который возвращал бы "BOSS".
     *
     * @return строка с читаемым названием ранга ("Босс" вместо "BOSS")
     */
    @Override
    public String toString() {
        return displayName;
    }
}
