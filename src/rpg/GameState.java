// ===== ЗАЧЕМ ЭТОТ ФАЙЛ? =====
//
// GameState — состояние игры в текущий момент: меню, исследование, бой или конец игры.
// Без него пришлось бы хранить состояние как число (0, 1, 2, 3) или строку ("menu", "battle"),
// что легко запутать и трудно поддерживать.
//
// enum гарантирует: GameState.BATTLE — всегда одно и то же значение, и ничего кроме.
// Кроме того, каждое состояние хранит дополнительные данные (displayName, active)
// и умеет проверять допустимость переходов (canTransitionTo) — это «умный» enum.
//
// Пакет rpg — все классы нашей RPG-игры (см. подробное объяснение в DamageType.java).
package rpg;

// ===== ПЕРЕЧИСЛЕНИЕ (enum) — НАБОР ИМЕНОВАННЫХ КОНСТАНТ =====
//
// enum (enumeration, перечисление) — специальный тип данных в Java,
// представляющий ФИКСИРОВАННЫЙ набор значений (констант).
//
// Зачем enum, если можно использовать целые числа (int)?
// Представим, что состояние игры кодируется числами: 0 = меню, 1 = исследование, 2 = бой.
//   int state = 0;
//   if (state == 3) { ... }  // Ошибка! 3 — не существующее состояние, но компилятор не заметит.
//   state = -1;              // Тоже ошибка! Отрицательное значение бессмысленно.
//   state = 999;             // И это пройдёт без ошибки...
//
// С enum такие ошибки НЕВОЗМОЖНЫ:
//   GameState state = GameState.MENU;
//   state = GameState.INVALID;  // Ошибка компиляции! INVALID не существует в enum.
//   state = 3;                  // Ошибка компиляции! int нельзя присвоить в GameState.
//
// Преимущества enum перед int-константами:
//   1. Типобезопасность — компилятор проверяет допустимые значения.
//   2. Читаемость — GameState.BATTLE понятнее, чем число 2.
//   3. Можно хранить данные — у каждой константы могут быть свои поля (displayName, active).
//   4. Можно добавлять методы — поведение прямо в enum.
//   5. Безопасно в switch — компилятор проверит, что обработаны ВСЕ варианты.
//
// ===== КАК УСТРОЕН enum ВНУТРИ? =====
//
// Каждый enum неявно наследуется от java.lang.Enum<GameState>.
// Это означает:
//   - enum НЕ МОЖЕТ наследовать другие классы (extends запрещён).
//     В Java нет множественного наследования классов, а Enum уже «занят».
//   - Но enum МОЖЕТ реализовать интерфейсы (implements разрешён):
//     public enum GameState implements Serializable, Comparable<GameState> { ... }
//     (Comparable уже реализован автоматически через Enum.)
//
// Каждая константа enum — это static final ЭКЗЕМПЛЯР (объект) этого enum.
// То есть GameState.MENU — это объект класса GameState, созданный один раз при загрузке класса.
// Нельзя создать новый экземпляр через new: new GameState() — ошибка компиляции.
//
// ===== ВСТРОЕННЫЕ МЕТОДЫ enum (наследуются от Enum) =====
//
// values()     — возвращает массив ВСЕХ констант: GameState[] all = GameState.values();
//                Порядок — такой же, как объявлены в коде (MENU, EXPLORING, BATTLE, GAME_OVER).
// valueOf("MENU") — возвращает константу по имени (строка должна ТОЧНО совпадать).
//                   Если имя не найдено — выбросит IllegalArgumentException.
// name()       — возвращает имя константы как строку: GameState.MENU.name() → "MENU".
// ordinal()    — возвращает порядковый номер (с нуля): MENU=0, EXPLORING=1, BATTLE=2, GAME_OVER=3.
//                ВНИМАНИЕ: ordinal() зависит от порядка объявления. Если переставить константы —
//                номера изменятся. Не используйте ordinal() для хранения в базе данных!
//
// GameState описывает состояния RPG-игры: меню, исследование, бой, конец игры.
public enum GameState {

    // ===== КОНСТАНТЫ enum =====
    //
    // Каждая строка — это ОБЪЯВЛЕНИЕ КОНСТАНТЫ (объекта) типа GameState.
    // Константы перечисляются через запятую и заканчиваются точкой с запятой
    // (если после них идут поля или методы).
    //
    // MENU("Главное меню", false) — это ВЫЗОВ КОНСТРУКТОРА GameState("Главное меню", false).
    // Каждая константа — это вызов конструктора с конкретными аргументами.
    // Конструктор создаёт объект GameState с заданными значениями полей.
    //
    // Константы enum — это static final поля. Это эквивалентно написанию:
    //   public static final GameState MENU = new GameState("Главное меню", false);
    //   public static final GameState EXPLORING = new GameState("Исследование", true);
    //   ...
    // Но Java делает это автоматически — достаточно перечислить значения.

    // Главное меню — игра ещё не началась или перезапускается.
    // active = false: в меню нет активного геймплея.
    MENU("Главное меню", false),

    // Исследование мира — герой путешествует, находит предметы, встречает NPC.
    // active = true: это активная фаза игры.
    EXPLORING("Исследование", true),

    // Боевая фаза — герой сражается с врагом.
    // active = true: бой — активная часть геймплея.
    BATTLE("Бой", true),

    // Конец игры — герой погиб.
    // active = false: геймплей завершён, игрок может только вернуться в меню.
    GAME_OVER("Игра окончена", false);

    // ===== ПОЛЯ enum =====
    //
    // У enum могут быть поля, как у обычного класса.
    // Поля хранят дополнительные данные для каждой константы.
    //
    // private final — поле доступно только внутри класса (private) и неизменяемо (final).
    // final означает: значение присваивается один раз (в конструкторе) и больше не меняется.
    // Это логично для enum — константа MENU всегда имеет displayName "Главное меню".

    // Отображаемое имя состояния на русском языке (для вывода в консоль).
    private final String displayName;

    // Флаг активного геймплея: true, если в этом состоянии идёт игровой процесс.
    // Используется, например, чтобы определить, нужно ли сохранять прогресс.
    private final boolean active;

    // ===== КОНСТРУКТОР enum =====
    //
    // Конструктор enum ВСЕГДА приватный (private), даже если не писать слово private.
    // Java запрещает вызывать конструктор enum извне — нельзя создать новые экземпляры.
    // Если написать public перед конструктором enum — ошибка компиляции!
    //
    // Конструктор вызывается АВТОМАТИЧЕСКИ для каждой константы:
    //   MENU("Главное меню", false)  →  вызывает GameState("Главное меню", false)
    //   EXPLORING("Исследование", true)  →  вызывает GameState("Исследование", true)
    //   и т.д.
    //
    // Количество параметров конструктора должно совпадать с аргументами в константах.
    // У нас 2 параметра (String displayName, boolean active) → каждая константа указывает 2 аргумента.
    GameState(String displayName, boolean active) {
        // this — ссылка на текущий объект (создаваемую константу enum).
        // this.displayName — поле объекта, displayName — параметр конструктора.
        this.displayName = displayName;
        this.active = active;
    }

    // ===== ГЕТТЕРЫ =====
    //
    // Геттеры предоставляют доступ на чтение к приватным полям (инкапсуляция).
    // Вызов: GameState.MENU.getDisplayName() → "Главное меню"

    /**
     * Возвращает отображаемое имя состояния на русском языке.
     *
     * @return строка с названием состояния для вывода в консоль
     */
    public String getDisplayName() {
        return displayName;
    }

    /**
     * Проверяет, является ли состояние активной фазой геймплея.
     *
     * @return true, если в этом состоянии идёт активная игра (EXPLORING или BATTLE)
     */
    public boolean isActive() {
        // Для boolean-геттеров используется префикс is (а не get):
        // isActive(), а НЕ getActive(). Это стандартное соглашение Java (JavaBeans).
        return active;
    }

    // ===== МЕТОД enum — ЛОГИКА ПЕРЕХОДОВ МЕЖДУ СОСТОЯНИЯМИ =====
    //
    // В enum можно добавлять методы, как в обычном классе.
    // Метод canTransitionTo() определяет, допустим ли переход из текущего состояния
    // в указанное следующее состояние. Это паттерн «конечный автомат» (state machine).
    //
    // Допустимые переходы:
    //   MENU       → EXPLORING    (начать игру)
    //   EXPLORING  → BATTLE       (встретить врага)
    //   EXPLORING  → GAME_OVER    (погибнуть от ловушки и т.д.)
    //   BATTLE     → EXPLORING    (победить врага и продолжить)
    //   BATTLE     → GAME_OVER    (проиграть бой)
    //   GAME_OVER  → MENU         (вернуться в главное меню)

    /**
     * Проверяет, допустим ли переход из текущего состояния в указанное.
     * Реализует логику конечного автомата (state machine) для состояний игры.
     *
     * @param next целевое состояние, в которое хотим перейти
     * @return true, если переход допустим
     */
    public boolean canTransitionTo(GameState next) {
        // ===== switch-ВЫРАЖЕНИЕ С enum (Java 14+) =====
        //
        // switch с enum — мощная конструкция. Особенности:
        //
        // 1. НЕ НУЖЕН default: если перечислены ВСЕ константы enum, компилятор
        //    гарантирует полноту. Если добавить новую константу и забыть обработать —
        //    ошибка компиляции! Это главное преимущество switch + enum.
        //
        // 2. Стрелочный синтаксис (->): вместо старого case X: ... break;
        //    используем case X -> выражение; Нет сквозного прохода (fall-through),
        //    break не нужен — каждая ветка изолирована.
        //
        // 3. switch как ВЫРАЖЕНИЕ (expression): возвращает значение (true/false).
        //    return switch (...) { ... } — результат switch подставляется в return.
        //    Раньше switch был только оператором (statement) и не мог возвращать значение.
        //
        // 4. В case пишем просто MENU (без GameState.MENU) — внутри switch по enum
        //    Java автоматически понимает, что MENU — это GameState.MENU.
        //
        // 5. Можно комбинировать: case EXPLORING, BATTLE -> ... (обработка нескольких
        //    констант одной веткой). Но здесь мы обрабатываем каждую отдельно.
        //
        // == для enum: сравнение через == работает корректно, потому что каждая
        // константа enum — это единственный экземпляр (синглтон). Два объекта MENU
        // не может существовать — всегда один и тот же объект.
        // Поэтому enum сравнивают через == (не через .equals()), хотя .equals() тоже работает.
        //
        // || — логическое ИЛИ (logical OR): true, если хотя бы один операнд true.
        return switch (this) {
            case MENU -> next == EXPLORING;
            case EXPLORING -> next == BATTLE || next == GAME_OVER;
            case BATTLE -> next == EXPLORING || next == GAME_OVER;
            case GAME_OVER -> next == MENU;
        };
    }
}
