// ===== ЗАЧЕМ ЭТОТ ИНТЕРФЕЙС? =====
//
// Healable («умеющий лечиться») — контракт для существ, способных восстанавливать здоровье.
// В нашей RPG лечиться могут воин, маг, лучник — но не каждый враг.
//
// Зачем Healable как ОТДЕЛЬНЫЙ интерфейс, а не часть Attackable?
// Потому что атака и лечение — разные способности. Паладин и атакует, и лечит → implements Attackable, Healable.
// Скелет-воин атакует, но не лечится → implements Attackable (без Healable).
// Разделение позволяет точно выразить способности каждого персонажа.
//
// Идиома «разделение интерфейсов» (Interface Segregation Principle):
// лучше несколько узких интерфейсов (Attackable, Healable), чем один широкий (CombatUnit с 10 методами).
// Тогда классы реализуют ТОЛЬКО нужные им способности.
//
// Пакет rpg (см. пояснение в DamageType.java).
package rpg;

// interface — контракт, описывающий способность (см. подробное объяснение в Attackable.java).
// Healable = «умеет лечиться». Реализуют классы, которые могут восстанавливать здоровье.
//
// Класс может реализовать несколько интерфейсов одновременно:
//   public class Paladin implements Attackable, Healable { ... }
// Это называется множественное наследование ПОВЕДЕНИЯ (в отличие от C++,
// Java не допускает множественное наследование классов, но допускает интерфейсов).
public interface Healable {

    // Абстрактный метод — метод без тела (см. подробное объяснение в Attackable.java).
    // Каждый класс, реализующий Healable, ОБЯЗАН написать реализацию heal().
    //
    // void — специальный «тип», означающий, что метод НИЧЕГО не возвращает.
    // Метод выполняет действие (лечит), но не отдаёт результат обратно вызывающему коду.
    // Сравни: int totalDamage() возвращает число, а void heal() — только выполняет действие.
    //
    // int amount — параметр: количество единиц здоровья для восстановления.
    // Реализующий класс сам решает, как применить лечение
    // (например, не превышая максимум здоровья).
    void heal(int amount);

    // default-метод — метод с реализацией в интерфейсе (см. подробное объяснение в Attackable.java).
    //
    // needsHealing() — проверяет, нужно ли персонажу лечение.
    // Реализация «по умолчанию» возвращает ВСЕГДА true — «безопасно-консервативное» поведение:
    // лучше предложить лечение лишний раз, чем пропустить, когда оно действительно нужно.
    //
    // Почему default, а не abstract?
    // Если бы метод был abstract, КАЖДЫЙ класс был бы обязан написать реализацию.
    // С default — можно и не переопределять: код всё равно скомпилируется.
    //
    // boolean — примитивный тип, хранит одно из двух значений: true (истина) или false (ложь).
    // Используется для условий: if (hero.needsHealing()) { hero.heal(50); }
    //
    // Реализующий класс ДОЛЖЕН переопределить этот метод для точной проверки:
    //   @Override
    //   public boolean needsHealing() {
    //       return currentHealth < maxHealth;  // лечение нужно, только если здоровье не полное
    //   }
    // Без переопределения — метод возвращает true даже для полностью здорового персонажа.
    // Это нормально в простых сценариях (heal() сам проверит max), но неоптимально.
    default boolean needsHealing() {
        return true;
    }

    // static-метод в интерфейсе (см. подробное объяснение в Attackable.java).
    // Вызывается через имя интерфейса: Healable.baseHealAmount(5)
    // Нельзя вызвать через объект: paladin.baseHealAmount(5) — ошибка компиляции!
    //
    // Утилитарная функция: рассчитывает базовое количество лечения по уровню персонажа.
    // Формула: уровень * 10. Персонаж 5-го уровня лечит 50 единиц здоровья.
    //
    // int level — параметр: уровень персонажа.
    // return level * 10 — возвращает рассчитанное количество лечения.
    static int baseHealAmount(int level) {
        return level * 10;
    }
}
